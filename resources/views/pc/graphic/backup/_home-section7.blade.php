            <p style="text-indent: 2em; text-align: center;">标普500指数预测美国大选</p>
            <p style="TEXT-INDENT:2em">美国智库Strategas Research Partners研究数据显示，使用标普500指数“总统预测模型”，1928年以来的准确率达到87%。1984年以来的准确率高达100%。根据该预测模型，如果标普500指数在美国大选日前90天内上涨，则执政党获胜，下跌则反对党获胜。简单的说总统连任首先股市就得先涨，若是股市下跌就必须换总统。 ?</p>
            <p style="TEXT-INDENT:2em">大选前90天指的是90个自然日，而非交易日，如2016年美国大选日为11月8日，则前90天为8月9日-11月7日，其他年份以此类推。</p>
            <p style="TEXT-INDENT:2em">2020年美国大选日为11月3日，则前90天为8月4日-11月2日，8月4日的标普500指数为3306，因此，如果标普500指数在11月2日能高于3306，则此“总统预测模型”将预测特朗普连任，否则美股的“钦定”总统则会改为拜登。</p>
            <p style="TEXT-INDENT:2em">2020年11月2日标普500指数为3310，两者几乎完全相同，轻微略涨，那就是难解难分，结果闹到最高法院，闹到国会，然而特朗普输了。</p>

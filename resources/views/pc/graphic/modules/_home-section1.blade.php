            <p style="TEXT-INDENT:2em">
                <a href="http://www.xixik.com/content/eea57ede8214fd77" title="美国总统届任位的区分">在提到美国总统的时候，往往会说到届、任、位</a>，他们具有各自不同的含义，因此理清它们之间的关系和区别十分必要。&#160;</p>
            <p style="TEXT-INDENT:2em">
                <strong>届：</strong>略同于“次”，用于表示定期会议、选举的次数。根据美国宪法第二十二条修正案，总统选举每四年举行一次，四年为一届，最多可以连任一届。如果总统在任期内死亡或因故去职，未能满任，另由他人接任，则继任总统和前总统属同一届总统。例如，约翰·肯尼迪在1963年11月遇刺身亡，林顿·约翰逊接任，他们二人均为第44届总统。</p>
            <p style="TEXT-INDENT:2em">美国总统一职根据1788年通过的美国宪法而设立，第一任总统华盛顿于1789年上任。当时规定
                <a href="http://www.xixik.com/content/55f02887dfbe55eb">美国总统每届任期为4年</a>。 但1787年宪法并没有明文限制总统的连任次数。</p>
            <p style="TEXT-INDENT:2em">1796年9月17日行将任满两届的首任总统乔治·华盛顿发表了著名的告别词，宣布不再竞选连任，从而开创了总统任期不超过两届的先例，以后历任总统都逐一效法。</p>
            <p style="TEXT-INDENT:2em">由于二战的特殊背景，富兰克林·罗斯福历任四届（1933-1945），创造了历史。所以到了1951年3月，生效的宪法第22条修正案规定：“任何人不得任总统之职两届以上，在另一人选为总统期内接任或代行总统职务两年以上者，不得再任总统之职一届以上。”</p>
            <p style="TEXT-INDENT:2em">
                <strong>任：</strong>有别于“届”，用于表示担任总统的次数而言。一个人如连续担任几届总统，仍被认为是一任。
                <a href="http://www.xixik.com/content/22d70046f63dfd8b">富兰克林·罗斯福就属于这种情况，他1933年至1945年连任4届总统</a>。但一个人如先后担任几届总统，但不属于连选连任，那么当选几次就算几任。
                <a href="http://www.xixik.com/content/3ae134ca204bf6b2">格罗弗·克利夫兰1885年至1889年和1893年至1897年两度入主白宫，因此算作两任</a>。 一般称美国总统为“第X任美国总统”比较多。</p>
            <p style="TEXT-INDENT:2em">
                <strong>位：</strong>指担任过总统的实际人数。不论是连选连任，还是先后当选几任，均不重复计算，有几人算几人。美国自1789年开始举行总统选举至今包括唐纳德·特朗普，已先后有44人担任过总统职务。</p>
            <p style="TEXT-INDENT:2em">比如：2000年当选的小布什是美国历史上第54届、第55届、第43任和第42位总统。2008年当选的奥巴马是美国历史上第56届、第44任和第43位总统，在2012年他连任了一届，故又加一届，第57届。2016年当选的唐纳德·特朗普是美国历史上第58届、第45任和第44位总统。2020年当选的乔·拜登是美国历史上第59届、第46任和第45位总统。</p>

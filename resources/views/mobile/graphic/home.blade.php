                    <a href="http://www.xixik.com/content/eefa3eca5832a980" title="中美破冰之旅：1972年，尼克松访华全程高清照片">尼克松访华开启中美破冰之旅</a>开始，此后中美高层交往开始频繁，除了卡特总统外，
                    <a href="http://www.xixik.com/content/8502b5aee3ad8eec">上任的美国总统都会访华</a>。截至2017年11月，
                    <a href="http://www.xixik.com/content/4dbe69bb4fb19211">历史上一共有8位美国总统访华</a>（尼克松、福特、里根、老布什、克林顿、小布什、奥巴马、特朗普），参观中国的名胜古迹是美国总统访华的传统，此前来访的8位美国总统都去了哪里？哪些地方最热门？又发表了什么话？
                    <a href="http://www.xixik.com/content/8e2bb01943234bce" style="text-indent: 2em;" target="_blank" title="美国历任总统的对华态度转变：由敌对化为伙伴">美国历任总统的对华态度转变：由敌对化为伙伴</a></p>
                <p style="TEXT-INDENT:2em">&ldquo;不到长城非好汉&rdquo;，美国总统访华最爱爬长城。被誉为&ldquo;世界建筑奇迹&rdquo;的
                    <a href="http://www.xixik.com/content/dcc76daf232aee45" title="了解中国象征万里长城的前生今世">万里长城是众多访华政要青睐的标志性景点</a>，也成为
                    <strong>
                        <a href="http://m.114.xixik.com/us-president/" style="color: rgb(0, 0, 0);" title="美国总统">美国总统</a></strong>访华的必游之地，尼克松、福特、里根、克林顿、小布什、奥巴马都在长城留下了自己的足迹。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>1、尼克松&mdash;&mdash;最早访华的
                        <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a></strong>
                </p>
                <p style="TEXT-INDENT:2em">访华时间：1972年2月21日至28日</p>
                <p style="TEXT-INDENT:2em">前往地点：北京、杭州、上海</p>
                <p style="TEXT-INDENT:2em">尼克松是红色中国成立以来首位访问中国的
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>。此行打开了中美两国关系正常化的大门，是二十世纪国际关系舞台上最重大的事件之一。</p>
                <p style="TEXT-INDENT:2em">访问期间，中共毛泽东会见了尼克松总统，周恩来与尼克松总统举行了会谈。两国领导人就中美关系和国际事务认真、坦率地交换了意见。2月28日，中美联合在上海发表举世瞩目的《
                    <a href="http://www.xixik.com/content/76139fa09a68b94e" title="中美联合公报">中美联合公报</a>》（《上海公报》）。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/5c61e-6621.jpg" /></p>
                <p style="TEXT-INDENT:2em">在中国的8天里，毛泽东、周恩来分别与尼克松进行会见和会谈。《中美联合公报》的发表标志着中美隔绝状态的结束和关系正常化进程的开始。为促进中美友好关系，中国政府将一对大熊猫&ldquo;兴兴&rdquo;和&ldquo;玲玲&rdquo;送给美国人民。</p>
                <p style="TEXT-INDENT:2em">声音：</p>
                <p style="TEXT-INDENT:2em">在北京，尼克松还登上了万里长城，他用这样的话形容当时的心情：&ldquo;当我在城墙上漫步时，我想到为了建筑这座城墙而付出的牺牲；我想到它所显示的在悠久的历史上始终保持独立的中国人民的决心&hellip;&hellip;长城告诉我们，中国有伟大的历史，建造这个世界奇迹的人民也有伟大的未来。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>2、福特&mdash;&mdash;&ldquo;破冰&rdquo;后首位访华的
                        <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a></strong>
                </p>
                <p style="TEXT-INDENT:2em">访华时间：1975年12月1日至5日</p>
                <p style="TEXT-INDENT:2em">前往地点：北京</p>
                <p style="TEXT-INDENT:2em">福特是中美关系正常化后第一位来华访问的
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>。毛泽东、邓小平分别与他进行了会见和会谈。双方重申遵守1972年《中美联合公报》。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/87ce6-4135.jpg" /></p>
                <p style="TEXT-INDENT:2em">在京期间，福特和夫人游览了天坛、长城、颐和园，观看了体育表演和现代舞剧。他的夫人贝蒂&middot;福特在参观舞蹈学校时，还跟学员一起跳舞。</p>
                <p style="TEXT-INDENT:2em">声音：</p>
                <p style="TEXT-INDENT:2em">福特在告别宴会上祝酒时说：&ldquo;我们的双边关系是重要的&hellip;&hellip;正是一些共同的认识和共同的利益使我们两国在四年前走到了一起。其中一点是双方同意不对别人称霸，并且根本反对别人把霸权强加在世界任何地方的努力。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>3、里根&mdash;&mdash;参观秦始皇陵兵马俑的&ldquo;老顽童&rdquo;</strong></p>
                <p style="TEXT-INDENT:2em">访华时间：1984年4月26日至5月1日</p>
                <p style="TEXT-INDENT:2em">前往地点：北京、西安、上海</p>
                <p style="TEXT-INDENT:2em">里根是1979年1月1日中美建交后首位在任时来华访问的
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>。里根表示美国将履行在三个联合公报中所承担的义务，执行&ldquo;一个中国&rdquo;的政策。双方签订了避免双重征税和防止偷漏税等四项协定和议定书，并草签了中美和平利用核能合作协定。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/876ff-1021.jpg" /></p>
                <p style="TEXT-INDENT:2em">1984年4月29日，里根与夫人南希在西安参观兵马俑时，下到十米深的秦俑坑，仔细欣赏了形态各异、栩栩如生的陶俑雕塑艺术。他们是&ldquo;心怀敬畏又带着奇思妙想&rdquo;地参观了这个景点。他问中方陪同人员：&ldquo;我能用手摸一摸这匹马吗？我猜它是不会踢我的。&rdquo;在得到同意后，他仔细观看了几个兵马俑，并且用手摸了摸。</p>
                <p style="TEXT-INDENT:2em">当他爬上木梯准备离开秦俑坑时，他又回头望了望宏伟的地下军阵，开玩笑说：&ldquo;解散！&rdquo; （Dismissed!）里根总统充分展现了自己诙谐幽默的一面。</p>
                <p style="TEXT-INDENT:2em">声音：里根在人民大会堂发表讲演时说：&ldquo;我们两国几乎是处在地球的两头，我们的语言、风俗和政治信仰可能很不相同。但是就我们这个时代的许多关键的问题来说，美中两国人民之间没有什么距离。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>4、老布什&mdash;&mdash;就职后&ldquo;最快&rdquo;访华的总统</strong></p>
                <p style="TEXT-INDENT:2em">访华时间：1989年2月25日至26日</p>
                <p style="TEXT-INDENT:2em">前往地点：北京</p>
                <p style="TEXT-INDENT:2em">乔治&middot;布什就任
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>刚满一个月就对中国进行了访问，成为在位期间&ldquo;最快访华&rdquo;的
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>，国家主席杨尚昆和邓小平等与他举行了会晤。</p>
                <p style="TEXT-INDENT:2em">1974年至1975年期间，
                    <a href="http://www.xixik.com/content/65c83c5944ae67ca" title="图说老布什的中国情缘 骑着自行车了解走街串巷">布什担任美国驻中华人民共和国联络处主任</a>。当时，他和夫人巴巴拉经常骑着自行车穿行于北京的大街小巷，既可健身，又能直接了解中国社会，接触普通百姓，可谓一举两得。他也因此被称为&ldquo;骑自行车的大使&rdquo;。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/ed2fc-6369.jpg" /></p>
                <p style="TEXT-INDENT:2em">而这次布什以总统的身份访华时，他和夫人获赠了两辆全新的&ldquo;飞鸽牌&rdquo;自行车。布什兴致勃勃地跨上车子，在众多记者面前摆出骑行的姿势。</p>
                <p style="TEXT-INDENT:2em">声音：</p>
                <p style="TEXT-INDENT:2em">在欢迎晚宴上，布什说：&ldquo;我们珍惜两国之间已经建立的新的关系，我们仍然信守构成我们关系基础的三个联合公报所确定的原则。我们根据只有一个中国的基本原则，已经找到了建设性地、不带恶意地讨论台湾问题的途径。美国现在关注，今后也关注由中国人自己和平解决自己的分歧。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>5、
                        <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>&mdash;&mdash;访华行程最丰富的总统</strong></p>
                <p style="TEXT-INDENT:2em">访华时间：1998年6月25日至7月3日</p>
                <p style="TEXT-INDENT:2em">前往地点：西安、北京、上海、桂林、香港</p>
                <p style="TEXT-INDENT:2em">9天内共到了西安、北京、上海、桂林、香港5个城市，参观兵马俑、故宫等古迹，在北京大学演讲，通过广播与上海市民&ldquo;空中对话&rdquo;，在烟雨中游览漓江&hellip;&hellip;
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>总统的访华日程格外充实。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/f30e3-3207.jpg" /></p>
                <p style="TEXT-INDENT:2em">1998年6月25日至7月3日，
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>对中国进行国事访问。6月27日，江泽民主席与
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>总统举行正式会谈。
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>总统重申美国坚持一个中国的政策，恪守中美三个联合公报的原则，并公开表示不支持台湾独立，不支持&ldquo;一中一台&rdquo;、&ldquo;两个中国&rdquo;，不支持台湾加入任何必须由主权国家才能参加的国际组织。即&ldquo;三不&rdquo;立场。双方就南亚问题、生物武器公约议定书和杀伤人员地雷问题发表了三个联合声明。
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>访华团超过1000人，当时创下了
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>访华的人数之&ldquo;最&rdquo;。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/4e86c-1362.jpg" /></p>
                <p style="TEXT-INDENT:2em">声音：</p>
                <p style="TEXT-INDENT:2em">在登长城时，
                    <a href="http://m.114.xixik.com/us-president/bill-clinton/" style="color:#000" title="克林顿">克林顿</a>说：&ldquo;如今长城是中国展示自己，而不再是把自己隔离在世界之外的象征。长城把中国凝聚起来了。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>6、
                        <a href="http://m.114.xixik.com/us-president/george-walker-bush/" style="color:#000" title="小布什总统">小布什</a>&mdash;&mdash;在位期间来华次数最多的
                        <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a></strong>
                </p>
                <p style="TEXT-INDENT:2em">访华时间：2001年10月18日至20日，2002年2月21至22日，2005年11月19日至21日，2008年8月7日至11日</p>
                <p style="TEXT-INDENT:2em">前往地点：上海、北京</p>
                <p style="TEXT-INDENT:2em">老布什的儿子、第43任
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>乔治&middot;沃克&middot;布什则创下了来华次数之&ldquo;最&rdquo;&mdash;&mdash;2001年来上海出席亚太经合组织领导人非正式会议（APEC）会议，2002年、2005年两次正式访问，2008年参加北京奥运会开幕式及相关活动，共计4次。在他之前的几位总统在任期内都只来华1次。</p>
                <p style="TEXT-INDENT:2em">2005年访华时，布什特意要求中方安排一次骑山地车锻炼。1975年的夏天，他来北京看望在美国驻华联络处工作的父母，骑着自行车逛遍了北京城，还在北京度过了29岁生日。2005年11月20日下午，布什实现了第二次在北京骑车的愿望，骑了1小时15分钟山地车。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/009cf-6680.jpg" /></p>
                <p style="TEXT-INDENT:2em">骑行时，他对身边陪同骑行的中国运动员说：&ldquo;2008年，我会来北京看奥运会。&rdquo;三年后，他如约而至，成为第一位在任期间出席在他国举办的奥运会的
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>。</p>
                <p style="TEXT-INDENT:2em">声音：2002年，布什在清华大学演讲时说：&ldquo;我们看到中国正日益成为世界上最富有活力和创造力的国家之一。中国正在兴起，而美国欢迎一个强大、和平与繁荣的中国出现。&rdquo;</p>
                <p style="TEXT-INDENT:2em">
                    <strong>7、
                        <a href="http://m.114.xixik.com/us-president/obama/" style="color:#000" title="奥巴马">奥巴马</a>&mdash;&mdash;同中国领导人会晤形式最多样的总统</strong></p>
                <p style="TEXT-INDENT:2em">访华时间：2009年11月15日至18日，2014年11月10日至12日，2016年9月3日至5日</p>
                <p style="TEXT-INDENT:2em">前往地点：北京、上海、杭州</p>
                <p style="TEXT-INDENT:2em">2009年11月15日至18日，是
                    <a href="http://m.114.xixik.com/us-president/" style="color:#000" title="美国总统">美国总统</a>
                    <a href="http://m.114.xixik.com/us-president/obama/" style="color:#000" title="奥巴马">奥巴马</a>上任后首次对中国进行国事访问。11月17日，胡锦涛主席与
                    <a href="http://m.114.xixik.com/us-president/obama/" style="color:#000" title="奥巴马">奥巴马</a>就中美关系及共同关心的重大国际和地区问题深入交换了意见，达成许多重要共识。双方一致同意，共同努力建设21世纪积极合作全面的中美关系，并将采取切实行动稳步建立应对共同挑战的伙伴关系。2014年11月10日至12日，
                    <a href="http://m.114.xixik.com/us-president/obama/" style="color:#000" title="奥巴马">奥巴马</a>来华出席亚太经合组织领导人非正式会议并对中国进行国事访问。2014年11月11日晚，在结束了APEC会议后，习近平主席与
                    <a href="http://m.114.xixik.com/us-president/obama/" style="color:#000" title="奥巴马">奥巴马</a>不打领带，在中南海瀛台举行了形式轻松、内容重要的会晤；2016年9月3日，在二十国集团（G20）杭州峰会期间，他们又在西子湖畔散步、茶叙。</p>
                <p style="TEXT-INDENT:2em">既有国事访问的正式活动，也安排一些轻松的场合，中美两国元首的互动模式越来越多样。</p>

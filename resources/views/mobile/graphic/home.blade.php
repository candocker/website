                <p style="TEXT-INDENT:2em">2020年美国大选日为11月3日，则前90天为8月4日-11月2日，8月4日的标普500指数为3306，因此，如果标普500指数在11月2日能高于3306，则此“总统预测模型”将预测特朗普连任，否则美股的“钦定”总统则会改为拜登。</p>

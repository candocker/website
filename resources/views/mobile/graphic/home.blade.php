                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/8ab4f-2677.jpeg" style="margin: 10px; float: right; width: 400px;" title="美国总统拜登" /></p>
                <p style="TEXT-INDENT:2em">乔·拜登（Joe Biden），全名小约瑟夫·罗宾内特·拜登（Joseph Robinette Biden, Jr），是美国第46任总统，民主党籍政治家，之前担任美国第47任副总统，特拉华州资深参议员。</p>
                <p style="TEXT-INDENT:2em">1942年11月20日在美国宾夕法尼亚州一个中产阶级家庭出生，1970年踏入政界 ，特拉华大学及雪城大学毕业，特拉华州律师和政治家。&#160;</p>
                <p style="TEXT-INDENT:2em">1972年当选美国参议员，是美国历史上排名第五的最年轻的参议员。也是特拉华州在任时间最长的参议员（1973-2009年）。1988年和2008年两度竞选美国总统，都没有成功，2008年和2012年两度作为美国前总统奥巴马的竞选搭档。曾任参议院司法委员会和对外关系委员会主席，在奥巴马执政时期任美国副总统。</p>
                <p style="TEXT-INDENT:2em">2019年4月25日，拜登宣布参选2020年美国总统。 2020年3月，拜登在密歇根州获民主党初选胜利。 2020年8月18日晚，美国民主党全国代表大会正式提名拜登为民主党总统候选人。 2020年是他第三次竞选总统。 2020年11月7日，据美国媒体测算，拜登已获得超过270张选举人票赢得大选。 2020年11月13日统计，目前全美所有州的选举人票归属已确定，拜登获得306张选举人票。</p></div>

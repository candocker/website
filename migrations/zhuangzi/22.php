	　　(20)无内：没有内容。
	　　(21)大初：天地未分前的浑饨状态，万物之本始，即指大道。大，作大。
	　　(22)昆仑：地之极高处，比喻有形与无形的分界处。大虚，广漠的虚空。
	　　[译文]
	　　妸荷甘与神农一同就学于老龙吉。神农凭靠小几，关起门昼寝。中午时候间荷甘推门而入说：“老龙死了！”神农凭几抱持手杖立起，嘭的一声放下手杖笑着 说：“先生知道我孤陋寡闻怠慢荒唐，所以弃我而死。完了，先生没有留下启发我的至言而死去了！”弇■吊听后说：“与道相合的人，是天下君子所归依之人。现 在他对于大道，连秋毫末端万分之一都未得到，还知道怀藏其至言而死去，又何况那些与道相合的人呐！道看起来无形，听起来无声，人们对它的种种议论，叫作暗 昧不明，他们所论述之道并不是真道。”
	　　于是泰清问无穷说：“您知道吗？”无穷说：“我不知。”又问于无为，无为说：“我知道。”又问：“您所知之道，也有什么道理规律吗？”回答说： “有”。又问：“它的道理规律是什么？”无为说：“我知道可处富贵，可处贫贱，可以收敛，可以分散，这就是我所知道之道理规律。”泰清把这话来问无始， 说：“如果是这样，则无穷之不知道与无为之知道，究竟谁是谁非呢？”无始说：“不知是对道知之甚深，知是对道所知极浅；不知是内心悟道，知是只了解一点道 的外在形式。”于是泰清仰天而叹说：“不知就是知吗？知就是不知吗？谁能知道不用名言相状表述之知是什么？”无始说：“道不可闻知，所闻知的都不是道；道 不可见，所见者都不是道；道不可言说，被言说出来的都不是道。需知创生有形万物的东西是无形的呀！道与它的名是不相应的。”无始说：“有人问道而给予应答 的，就是不知道；就是那个问道之人，也是没听说过道。道是不能问的，有问也不应回答。本不可问又要问，这种问是空的；本不应回答而回答，这种回答是没有内 容的。以没有真实内容的回答去对空问，如果这样，对外不能观察宇宙之无限，对内不能了解道之根本。因此他不能超越有形之界域，不能逍遥于广漠之虚空。”
	　　光曜问乎无有曰(1)：“夫子有乎？其无有乎？”光曜不得问而孰视其状貌(2)，窅然空然(3)，终日视之而不见，听之而不闻，搏之而不得也(4)。 光曜曰：“至矣，其孰能至此乎！予能有无矣，而未能无无也(5)。及为无有矣，何从至此哉(6)！”大马之捶钩者(7)，年八十矣，而不失豪芒(8)。大 马曰：“子巧与？有道与？”曰：“臣有守也(9)。臣之年二十而好捶钩，于物无视也(10)，非钩无察也。”是用之者假不用者也(11)，以长得其用 (12)，而况乎无不用者乎(13)！物孰不资焉！
	　　[注释]
	　　(1)光曜、无有:皆虚拟之名。
	　　(2)孰，通熟。熟视：仔细观察。
	　　(3)窅（yǎo）然：隐晦不明之状，亦为空寂之意。
	　　(4)搏：触摸。
	　　(5)有无：光曜无形体，听不到摸不着，故言有无。但还可以看见，未达绝无形迹之无无。
	　　(6)为无有：既不执着于无，也不执着于有，而是有无双遗，超越二者，达到一个更高境界。此种境界一般人是无从达到的。
	　　(7)大马：官名，指楚国之大司马。捶：锻造。钩：剑名。
	　　(8)豪芒：锋利有光芒。
	　　(9)守：持守，毕生专心持守于此。
	　　(10)于物无视：对别的东西部视而不见，一心只在造钧上。
	　　(11)用之者：指打造钩的技艺。假：借助、凭借。不用之者：指平时于物无视，专注于此道。
	　　(12)长：长期。
	　　(13)无不用：于物皆不用心，而至于无无之境，达于至道之域，则万物无不资取于他。
	　　[译文]
	　　光曜问无有说：“先生是有呢？还是无有呢？”光曜没有得到回答而仔细观察其状貌，隐晦空寂的样子，整天看它也看不见，听也听不到，摸也摸不着。光曜 说：”达到极致了，谁能至于此境啊！我能达到无，而未能达到无无。及至超越有无之境。又从何达到呀！”楚国大司马有一位造剑的人，已经八十岁了，造出之剑 仍然锋利有光芒。大司马说：“你是技艺高呢？还是其中有什么道理？”回答说：“臣有所持守。臣在二十岁时就喜好造剑，对他物视而不见，不是剑就不去察 看。”这是造剑之技艺借助于平时的专注上，因此能长期得以熟练运用，何况于物皆不用心而达于至道的人呢！万物谁不资借于他呀！
	　　冉求问于仲尼曰：“未有天地可知邪？”仲尼曰：“可。古犹今也。”冉求失问而退(1)。明日复见，曰：“昔者吾问：‘未有天地可知乎？’夫子曰： ‘可，古犹今也。’昔日吾昭然，今日吾昧然(2)，敢问何谓也？”仲尼曰：“昔之昭然也，神者先受之(3)；今之昧然也，且又为不神者求邪(4)？无古无 今，无始无终。未有子孙而有子孙可乎(5)？”冉求未对。仲尼曰：“已矣，未应矣(6)！不以生生死(7)，不以死死生(8)。死生有待邪？皆有所一体 (9)。有先天地生者物邪(10)？物物者非物(11)，物出不得先物也(12)，犹其有物也(13)。犹其有物也，无已(14)。圣人之爱人也，终无己 者，亦乃取于是者也(15)。”
	　　[注释]
	　　(1)失问：失去问意。心有所悟，不想再问。
	　　(2)昭然：明白。昧然：胡涂。
	　　(3)神者先受之：用空虚的心神先接受领会。
	　　(4)不神者求：不神者指外界事物及道理。向外界事物道理去寻求验证，所以变得胡涂了。
	　　(5)这句意思是，古有子孙，于是代代繁衍，今天才有子孙。如果古无子孙，今日也不会凭空生出子孙。由此推证，古代和今天相同，今天即是古代的继续。
	　　(6)未应：不要应答，待继续讲说下去。
	　　(7)不以生生死：死者自行死去，新生者并不是使已死者复生。
	　　(8)不以死死生：新生者自生，死去者也不是使新生者死去。
	　　(9)待：相互依赖、相互依存之意。死与生并不相互依赖，它们各有自己的体系。
	　　(10)者：作之解。这句意思是：有先于天地就生成之物吗？
	　　(11)物物者非物：生成物的那个东西，不是物自身，而是物之它体。
	　　(12)物不得先物：被生成之物不能先于生成它的物而存在。譬如生物由细胞生成，细胞由分子生成，分子由更小的东西生成等等，这样生物不能先于细胞， 细胞不能先于构成它的分子，分子不能先于构成它的更小原素，这样追溯下去，以至于无穷。如《齐物论》所说：“有有也者，有无也者，有未始有无也者，有未始 有夫未始有无也者”，是没有穷尽的。在这无穷系列中，后一环节之物下能先于前一环节之物存在，即是“物不得先物”。
	　　(13)犹其有物：生成此物的物，上面仍然还有它的生成者。犹，依然、仍然。
	　　(14)无已：没有止境。
	　　(15)取于是：圣人即是取法于自然之理，故其爱人类无止境。是，指上面所说自然之理。
	　　[译文]
	　　冉求问孔子说：“未有天地以前的情形可以知道吗？”孔子说：“可以。古代和现今相同。”冉求不想再问而退去。第二天又来相见，说：“昨天我问‘没有天 地以前的情形可以知道吗？’先生说：‘可以。古代和今天相同。’昨天我还明白，今天我又胡涂了。请问这是为何呢？”孔子说：“昨天你明白，是用空虚之心神 先加接受和领会它；今天又胡涂，则是向外界事物道理寻求验证之故啊！没有古也没有今，没有开始也没有终结。如果说以前没有子孙而今天有了子孙，这样说可以 吗？”冉求没有回答。孔子说：“算了，先不要应答！死者自死，新生者不是使已死者复生；生者自生，死者也不是使新生者死去，死生是相互依赖吗？它们并不依 赖而各有体系。有先于天地就生成之物吗？生成物的那个东西一定不是物自身，被生成之物不得先于生成它的物而存在，生成物上面仍然还有生成者，生成者上面仍 然有生成者，是没有止境的呀！圣人的爱人类没有止境，也就是取法于此启然之理。”
	　　颜渊问乎仲尼曰：“回尝闻诸夫子曰：‘无有所将，无有所迎(1)。’回敢问其游(2)。”仲尼曰：“古之人外化而内不化(3)，今之人内化而外不化。 与物化者，一下化者也(4)。安化安不化(5)，安与之相靡(6)，必与之莫多(7)。豨韦氏之囿(8)，黄帝之圃，有虞氏之宫，汤武之室(9)。君子之 人，若儒墨者师，故以是非相■也(10)，而况今之人乎！圣人处物不伤物。不伤物者，物亦不能伤也。唯无所伤者，为能与人相将迎。山林与，皋壤与 (11)，使我欣欣然而乐与！乐未毕也，哀又继之。哀乐之来，吾不能御，其去弗能止。悲夫，世人直为物逆旅耳(12)！夫知遇而不知所不遇(13)，知能 能而不能所不能(14)。无知无能者，固人之所不免也。夫务免乎人之所不免者(15)，岂不亦悲哉！至言去言，至为去为(16)。齐知之所知(17)，则 浅矣。”
	　　[注释]
	　　(1)将：送。这句意思是，不送不迎，听其自然。
	　　(2)游：指精神之出入自在。
	　　(3)外化：随顺外物之变化而变化。内不化：内心平静安宁、恒定不变。
	　　(4)一不化，恒常保持淡漠无心，即是一下比，有此则可与物化。
	　　(5)安，习惯。这句意思是，不管化与不化，皆能习惯自处。
	　　(6)靡，顺。
	　　(7)莫多：不增益。循物之性，顺其自然，不予增减。
	　　(8)豨韦氏：远古之帝王，又见《大宗师》篇。囿：古代帝王畜养禽兽之园林。
	　　(9)囿、圃、宫、室：皆指帝王居处游息之所。圃比囿小、宫比囿小，室比宫小。居处之所愈小，精神愈狭隘，道德愈衰落。
	　　(10)■（jī）：和。这句意思是，儒墨二家之师，是非对立最难调和，古之君之亦能顺应调和，何况今人之一般争论呢。
	　　(11)皋壤：平原。
	　　(12)直：但。逆旅：旅舍。
	　　(13)遇：遭遇，接触。这句意思是，遇到则知，不遇则不知。遇有限，知亦有限。
	　　(14)能能：能作到力所能及的。
	　　(15)人之所不免：人有所知所能，亦有不知不能，不知不能是人所不能避免的。庄子认为有的入却要强求避免，劳心弗力以自逞，为害生之道。
	　　(16)至言去言：至道之言去掉言说。至为去为：至道之为去掉有为。
	　　(17)齐：齐一。知之所知：靠主体与外界接触所得之知，是靠学习和教化所得有形迹之知，这种知是浅陋的。
	　　[译文]
	　　颜渊问孔子说：“我曾经听老师说：‘不要有所送，不要有所迎。’我请问如何能使精神出入自如。”孔子说：“古时之人随顺物化而内心安定不变，现今之人 内心游移不定而又执滞外物不能顺应其变化。能随顺外物变化的，一定是内心淡漠安定之人。不管是变化还是不变，都能习惯自处，习惯与其相顺应，参与变化而不 加增益。豨韦氏的园林，黄帝的园圃，虞舜之宫殿，汤武之宫室，居住愈狭小而道德愈低下。称得上君子之人，就是对儒墨老师那样对立，也能使其是非相互调和， 何况是对待今人之争论呢！圣人与物相处而不伤害物。不伤害物的人，物也不能伤害他，只有无所伤害的人，才能与人相交往。山林啊，平原啊，都能使我欣然快 乐！快乐还没有完毕，悲哀又继之而来，悲哀与快乐的到来，我不能抗拒，其离去我也不能阻止。多么可悲，世人之心只是为悲哀欢乐提供之旅舍罢了！他们只知所 遭遇到的，不知所未曾遭遇到的；只能作到力所能及的，不能作到力所不及的。有所不知有所不能，本来就是人所不能避免的。那些人强求避免人所不能避免的方 面，岂不也是可悲的公！至道之言去掉言说，至道之为去掉有为。想齐一人们所得之知，则是浅陋的。


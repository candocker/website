	　　东郭子问于庄子曰(1)：“所谓道，恶乎在？”庄子曰：“无所不在。”东郭子曰：“期而后可(2)。”庄子曰：“在蝼蚁。”曰，“何其下邪？”曰： “在稊稗(3)。”曰：“何其愈下邪？”曰：“在瓦瓷(4)。”曰：“何其愈甚邪？”曰：“在屎溺。”东郭子不应。庄子曰：“夫子之问也，固不及质 (5)。正获之问于监市履豨也(6)，每下愈况(7)。汝唯莫必(8)，无乎逃物(9)。至道若是，大言亦然(10)。周遍咸三者(11)，异名同实，其 指一也。尝相与游乎无何有之宫(12)，同合而论(13)，无所终穷乎！尝相与无为乎！澹而静乎(14)！漠而清乎(15)！调而闲乎(16)！寥已吾志 (17)，无往焉而不知其所至(18)，去而来而不知其所止，吾已往来焉而不知其所终；彷徨乎冯闳(19)，大知入焉而不知其所穷(20)。物物者与物无 际(21)，而物有际者，所谓物际者也(22)。不际之际，际之不际者也(23)。谓盈虚衰杀(24)，彼为盈虚非盈虚(25)，彼为衰杀非衰杀，彼为本 末非本末，彼为积散非积散也。”


	　　[题解]
	　　本篇取开头三字“知北游”为篇名，是《庄子》中比较重要的一篇。集中阐述庄子和道家哲学的宇宙论、认识论。对最高本体的道作了较多论述，提出道是万物 之本，既产生万物，又在万物之中，并主宰其运动变化，天地万物都不能离开道。还论述道是不可知的，并对事物矛盾对立，新故相除，死生交替的发展观作了多方 面阐述，这些思想对后代哲学发展有多方面影响。
	　　全篇粗略分为十段，第一段较长，带有全篇总论性质。其中心有二点，一是道不可知，凡讲说出来的都不是真道；二是提出“通天下一气耳”的重要命题，把人 之生死，万物生灭都看作气的聚散，把一切统一于气，气又根源于道，回答了万物统一性的问题。这些思想在以后各段不断得到充实。第二段，圣人效法天道而行无 为之治，天地阴阳四时各得道而自足。第三段，啮缺问道于被衣，提出真正知道者应是“形如槁骸，心若死灰”，暗昧无心的样子。第四段，提出人的形体、生命、 性命都非自有，而是受天地造化支配的。
	　　第五段内容较复杂，讲了有形生于无形，精神生于道，万物生于精，并以形相生的宇宙生成论问题。还讲天地日用万物得道而成，生死只是自然转化过程，用不 着为此而忧乐。第六段，道无所不在，即在万物中，又支配万物的运动变化过程。第七段，包括两个故事，一个通过弇■吊之口，讲述道“视之无形，听之无声”， 人们所议论的道，都是非道。一个故事通过泰清与无穷等的对话，又进一步阐述道不可闻、不可见、不可言。没有任何名言与道相符。因此，靠闻见言说不能达于至 道。第八段，亦有二个故事。光曜问无有，讲至道不执著于有无。另一个故事写捶钩老人精神专注而技艺如神，推证不分心于外物，则得大道而无不通。第九段，冉 求与孔子讨论天地之始观问题，提出“古犹今也”，没有分别。还提出死主各自成体和“物物者非物”的生死观和宇宙观问题。第十段，持守无为之道，不强求其所 不知不能，“至言去言，至为去为”，不送不迎，听其自然，即可得道。
	　　知北游于玄水之上(1)，登蹲弅之丘(2)，而适遭无为谓焉(3)。知谓无为谓曰：“予欲有问乎若：何思何虑则知道？何处何服则安道(4)？何从何道 则得道(5)？”三问，而无为谓不答也。非不答，不知答也(6)。知不得问，反于白水之南(7)，登狐阕之上(8)，而睹狂屈焉(9)。知以之言也问乎狂 屈，狂屈曰：“唉！予知之，将语若。”中欲言而忘其所欲言(10)。知不得问，反于帝宫，见黄帝而问焉。黄帝曰：“无思无虑始知道，无处无服始安道，无从 无道始得道。”知问黄帝曰：“我与若知之，彼与彼不知也(11)，其孰是邪？”黄帝曰：“彼无为谓真是也，狂屈似之，我与汝终不近也(12)。夫知者不 言，言者不知，故圣人行不言之教(13)。道不可致(14),德不可至(15)。仁可为也(16)，义可亏也(17)，礼相伪也(18)。故曰：‘失道而 后德，失德而后仁，失仁而后义，失义而后礼(19)。’礼者，道之华而乱之首也(20)。故曰：‘为道者日报，损之又损之，以至于无为，无为而无不为也 (21)。’今已为物也(22)，欲复归根，不亦难乎！其易也，其唯大人乎(23)！生也死之徒(24)，死也生之始，孰知其纪(25)！人之生，气之聚 也；聚则为生，散则为死。若死生为徒，吾又何患！故万物一也(26)。是其所美者为神奇，其所恶者为臭腐(27)。臭腐复化为神奇，神奇复化为臭腐 (28)。故曰，‘通天下一气耳(29)。’圣人故贵一。”知谓黄帝曰：“吾问无为谓，无为谓不应我，非不我应，不知应我也；吾问狂屈，狂屈中欲告我而不 我告(30)，非不我告，中欲告而忘之也；今予问乎若，若知之，奚故不近(31)？”黄帝曰：“彼其真是也，以其不知也(32)；此其似之也，以其忘之 也；予与若终不近也，以其知之也。”狂屈闻之，以黄帝为知言(33)。
	　　[注释]
	　　(1)知：虚拟人名。玄水：虚拟河流名。
	　　(2)隐彝（fén）：假设之地名。
	　　(3)无为谓：虚拟之得道者，与自然合一无为不言之人。
	　　(4)居：居处。服：行事。安：持守。
	　　(5)何从问道：由何种途径。用何种方法。
	　　(6)不知答：意思是说，无为谓视大地万物为一体，无分别之心，故对所问不知答。
	　　(7)白水：传说中的河流名，与玄水相对。
	　　(8)狐阕：虚拟的山名。
	　　(9)狂屈：虚拟人名。本篇所举之人名、地名、河流名多为虚拟，并含有寓义。
	　　(10)中欲言：正想说的当中。
	　　(11)波与彼：指无为谓与狂屈。
	　　(12)不近：与道不相近。
	　　(13)不言之教：不用言语的教化。
	　　(14)致：招致、取得。
	　　(15)至：达到。这句意思是，道与德不能有意求得，愈是有意追求，愈离道德遥远。无为无求，与天地同一，则道致而德达。
	　　(16)仁：指儒家之仁，是有形迹的，可有意去作到。
	　　(17)亏：损弃。义：裁断是非的标准。庄子认为，裁断中取其合宜、弃其不宜，故有损弃。
	　　(18)礼相伪：礼是人制定的社会、道德规范，在推行中重表面形式，不重内在真实，故易流于相互欺骗和诈伪。
	　　(19)出自《老子》三十八章。意为道德仁义礼的相继出现，反映社会由无为进入有为，随着文明的进步，智力的发展，人距离纯真质朴之性愈远，道德也不断下降。只有废止一切文明成果，反朴归真，回复自然，才能达到道德的完善。
	　　(20)华：同花。比喻漂亮的外在形式。庄子认为，礼是在人与人之间失掉忠信后制定出来，起约束作用的。既对人的行为进行约束限制，就必出现种种形式的反限制，从而发起纷争和动乱。所以说礼是“乱之首”。
	　　(21)损：减损，指减损人之知识、经验、欲望等。无为而无不为：因任自然，不加干预，则万物各循其性、自行主化、天不自为而成。
	　　(22)今已为物：现已成有形之物。即由虚无之道聚而成体，再复归虚无则难。
	　　(23)大人：至人，与天道无为一体，故复归大道则易。
	　　(24)徒：类。这句的意思是，生与死为同类，就一物说有生死之别，就万物总体说则无生死之分，此物之生或为彼物之死，生死为同类。
	　　(25)纪：纲纪、条理。
	　　(26)万物一也：气之聚散表现为物生死之无穷变化过程，万物统一于气。
	　　(27)这句是说：人们把自己认为美好的称神奇，把自己厌恶的称为臭腐。神奇与臭腐本没有同一的客观标准。
	　　(28)神奇与臭腐可以互相转化，从转化观点看，二者又是齐一的。
	　　(29)通天下一气：把天地万物看成一气贯通，这种观点包含某种唯物论因素，但气并未脱离虚无之道的笼罩，气不过是道的体现，道的作用而已。通，贯通。
	　　(30)不我告：不告诉我。
	　　(31)奚：何，不近：与大道不相近。
	　　(32)彼：指无为谓。
	　　(33)知言：懂得知者不言、言者不知的道理。
	　　[译文]
	　　知北游到玄水边，登上隐弅山丘，而恰巧碰到了无为谓。知对无为谓说：“我想问一问你：怎样思索怎样考虑则可认识道？如何居处如何行事则可持守道？由何 种途径用何种方法则可获得道？”问了三次无为谓不回答，不是不回答，而是不知道要回答。知之问题未得解答，就返回到白水的南面，登上狐阕山丘，而看见了狂 屈。知又把那三个问题来问狂屈，狂屈说：“唉！我知道，就告诉你。”正想说的当中忘记了所要说的内容。知未得回答，又返回帝宫，见到黄帝又问及那三个问 题。黄帝说：“无恩无虑才能认识道，无定处不行事才能持守道，不要任何途径和方法才能获得道。”知问黄帝说：“我和您知道这些，无为谓和狂屈却不知道，谁 是对的呢？”黄帝说：“那个无为谓是真正对的，狂屈接近于正确，我和你终究与道不相近。知道者不言，言道者不知，所以圣人推行不言之教化。道是不能获取 的，德是不能达到的。仁可以去做，义可以损弃，礼是相互欺骗的。所以说：‘失去道而后才有德，失去德而后才有仁，失去仁而后才有义，失去义而后才有礼。’ 礼只是道华丽的外表，而为祸乱之开端，所以说：‘从事于道要天天减损，减损而又减损，以达到无为，无为而后方能无不为。’现已成为有形之物，要想返回虚无 之本恨，不也是很难的么！如果说容易作到的话，那只有得道之至人啊！生为死之同类，死为生之开始，谁能知道其条理伦序！人之出生是气之聚合。气聚台则得 生，散灭就死亡。如果死生是同类的，我还有什么担心呢！所以万物是一体的。人们把自己认为美好的称神奇，把自己厌恶的称臭腐。臭腐可以转化为神奇，神奇可 以转化为臭腐。所以说：‘贯通天下只是一气而已。’因而圣人重视一。”知对黄帝说：“我问无为谓，无为谓不回答我，不是不回答我，不知道要回答；我问狂 屈，狂屈欲说给我又不说了，不是不说，是欲说当中忘记所要说的了；现在我问于您，您知道这么多，何故说是与大道不相近呢？”黄帝说：“无为谓是真正知道 者，因为他不知；狂屈近似于知道，因为忘记所知；我和你终究与道不相近，因为是知。”狂屈听到后，认为黄帝是知言的。
	　　天地有大美而不言(1)，四时有明法而不议(2)，万物有成理而不说(3)。圣人者，原天地之美而达万物之理(4)。是故至人无为，大圣不作，观于天 地之谓也。今彼神明至精(5)，与彼百化(6)。物已死生方圆(7)，莫知其根也；扁然而万物自古以固存(8)。六合为巨(9)，未离其内(10)；秋毫 为小，待之成体。天下莫不沈浮(11)，终身不故(12);阴阳四时运行，各得其序；惛然若亡而存(13)，油然不形而神(14)，万物畜而不知 (15)。此之谓本恨，可以观于天矣(16)。
	　　[注释]
	　　(1)大美：指夭地覆载万物，生养万物而又不自居其功，具有最大美德。
	　　(2)明法：明确的规律。
	　　(3)成理，万物生成之理。
	　　(4)原：归本、推究之意。达：通达。
	　　(5)彼：指天地。神明：天地蕴含的活力、创造力，虽无形可见却无所不在，主宰一切，它是极精微的。
	　　(6)彼，指万物。与彼百化：天地参与万物之各种变化。
	　　(7)死生方圆：物或生或灭，或方或圆，变化无方，形态各异，莫知其所由来。
	　　(8)扁然：犹遍然，普遍地。
	　　(9)六合：上下四方的无限空间。巨：巨大。
	　　(10)其：指道。这句话是说，六合虽巨大，亦在大道中。
	　　(11)沈浮：升降、往来。表示万物的相互作用与无穷变化。沈，通沉。
	　　(12)故：陈旧，不故：言其新故相除，永葆生机。
	　　(13)惛然，暗昧之状。形容大道暗昧模糊，似亡而存的样子。
	　　(14)油然：流动变化无所系着之状。
	　　(15)万物畜：万物为其畜养。
	　　(16)观于天：观见自然之道。
	　　[译文]
	　　天地有最大的美德而不言说，四时有明确的规律而不议论，万物有生成之理而不解说。圣人推究天地之美德而通达万物生成之理。所以至人自然无为，大圣人不 造作，观察天地之道加以效法而已，现今天地之神明极精微，参予万物的无穷变化；物：发生或生或灭或方或圆的变化，没有办法知道它的根源；万物自古以来原本 就这样普遍存在着。六合虽然巨大，未超出道之外；秋毫虽小，待道而成形体。天下万物无不在升降变化，终生都不会陈旧；阴阳四时运行，各得其秩序；大道暗昧 模糊似亡而存，流动变化没有形状而神妙莫测，万物为其畜养而不知。这就叫作本根，可以由此观见自然之道。
	　　啮缺问道乎被衣(1)，被衣曰：“若正汝形，一汝视，天和将至(2)；摄汝知，一汝度，神将来舍(3)；德将为汝美，道将为汝居(4)。汝瞳焉如新生 之犊(5)，而无求其故(6)。”言未卒，啮缺睡寐(7)。被衣大说，行歌而去之，曰：“形若槁骸，心若死灰(8)，真其实知(9)，不以故自持 (10)。媒媒晦晦(11)，无心而不可与谋，彼何人哉(12)！”
	　　[注释]
	　　(1)被衣：虚拟之人名。据《天地》篇，彼衣是王倪的老师，啮缺是王倪的弟子。啮缺还见于《齐物论》等篇。
	　　(2)若：你。天和将至，天然之和气就会到来。
	　　(3)摄：收敛，一汝度：使思虑专一之意。神：神明之精，即道之功能活力。
	　　(4)居：居处。
	　　(5)瞳（tóng）然，无知直视的样子。犊：小牛。
	　　(6)故：原由。无求其故，不追究事物原由，漠然置之，听其自然。
	　　(7)卒：终。睡寐：睡着了。
	　　(8)槁骸：枯骨，心若死灰：形容心枯寂不动，没有生机，象完全死灭之灰。
	　　(9)真其实知：真正纯实之知。形如槁木，心如死灰。无知无虑，方是真知道。
	　　(10)不以故自持：不固守故见，与变化同步。
	　　(11)媒媒晦晦：懵懂无知的样子。媒，作昧。
	　　(12)彼何人哉，他是个什么人啊！表达惊叹赞许之意。
	　　[译文]
	　　啮缺问道于被衣，被衣说：“你要端正你的形体，集中你的视线，天然之和气就会前来；收敛你的智慧，专一你的思虑，神明就会来居留你心；德将表现你之美 好，道将留在你的身上。你无知而直视的样子就象初生的小牛犊，你不要去追究事物的原由。”话未说完，啮缺已经睡着了。被衣特别高兴，一边走一边唱歌而去， 还说：“形体如同枯骨，心如同死灰，真正纯实之知，不坚持故见，懵懂暗昧，没有思想，不能和他计议谋划，他是个什么样人啊！”
	　　舜问乎丞曰(1)：“道可得而有乎？”曰：“汝身非汝有也，汝何得有夫道！”舜曰：“吾身非吾有也，孰有之哉？”曰：“是天地之委形也(2)；生非汝 有，是天地之委和也(3)；性命非汝有，是天地之委顺也(4)，子孙非汝有，是天地之委蜕也(5)。故行不知所往，处不知所持，食不知所味(6)。天地之 强阳气也(7)，又胡可得而有邪！”
	　　[注释]
	　　(1)丞：古之得道者，舜之师。又说为官名。古代帝王有四辅之官，左辅右粥前疑后丞，丞即四辅之一。
	　　(2)委形：寄托给你一个形体。委，寄托。
	　　(3)和：和气
	　　(4)委顺：寄托给你顺任自然之性，于是乃有性命。顺，顺任自然。
	　　(5)蜕：蜕变。指生物之脱皮生新。此处比喻人的子孙繁衍能力，也是天托寄给人的。
	　　(6)持：持守。这句意思是说，人的行、住、食都不属于自己，而受天支配。
	　　(7)强阳气：强健运动之气。即天地阴阳二气聚合运动主宰支配一切。
	　　[译文]
	　　舜问丞说：“道可以获得和拥有吗？”回答说：“你的身体都不是你所拥有，你怎么能拥有道呢！”舜说：“我的身体非我所有，归谁所有呢？”回答说：“是 夭地寄托给你一个形体；生命非你所有，是天地寄托给你和气；性命非你所有，是天地寄托给你顺应自然之属性；子孙非你所有，是天地寄托给你繁衍子孙的能力。 所以行时不知往哪里去，住时不知持守什么，吃东西不知味道。这一切都受强健运动之气所支配，又怎么能获得和拥有呢！”

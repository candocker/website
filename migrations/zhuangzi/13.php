	　　(32)倒道：与道相反相违。指违背大道由本及未的先后次第，把形名赏罚等次要的、枝节的东西提到首位，即是倒道。
	　　(33)迕，违逆。迕道，与倒道同义。
	　　(34)人之所治：被人治理。
	　　(35)知治之具：只知治世的具体方法、手段，不知精神实质，知未而不知本。
	　　(36)这句的意思力：可用于为天下事奔波操劳，不足以让天下自行治理，可有力而治而不能无为而治。

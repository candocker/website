	　　(12)迂：迂远。庄子认为：行虚静无为之道，则无有不爱，何心又说兼爱，既讲兼爱，则有兼之所不及者，因此反而更为迂远。
	　　(13)这句的意思为：私与无私的区分与对立，正是私产生的根源。讲无私即包含有私，只有混同私与无私，抹灭二者对立，才能达到真正无私。

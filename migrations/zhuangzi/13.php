	　　[译文]
	　　士成绮见老子问道：“我听说先生是圣人，故而我不辞路远而来，期望见到您，走了百舍路程，脚上磨出层层老茧也不敢停下。现在我看您不是圣人，您家鼠洞 口积土狼藉着菜蔬，却抛弃妹妹不肯供养，这是不仁慈！生的和熟的食品摆在面前，享用不尽，还屯积聚敛财物无止境。”老子表情冷淡不回答。士成绩第二天又来 相见，说：“上次我曾伤害过您，现在我的这种心情正在退去，这是什么原因呢？“老子说：“巧智神圣那样的人，我自以为已经从那里面摆脱出来了。以前你称呼 我为牛我也自认为牛；称呼我为马我也自认为马。假如确有那样的事实，别人加给他名称又不肯接受，这是再次遭受祸殃。我的仪态行为是恒常不变的，我不是有意 作出某种仪态行为给别人看。”士成绮在斜后方跟随，避开老子的身影，未脱鞋子就入室走近老子问道，怎样修身呢？老子说：“你的仪容庄重严肃，你的眼睛专注 直视，你的前额高高扬起，你的言论凶猛横暴，你的体形巍峨高大。就象奔马被系缚而停止下来，想动而受到限制，一旦发动就象扣动弯机一般疾速，对事物明察而 又精细，智巧过人而表现骄傲放肆神态，以上所为皆出于有意造作，不合自性，故不可信。边境上如果有这样有意造作之人，就称之为贼。”
	　　夫子曰：“夫道，于大不终，于小不遗(1)，故万物备。广广乎其无不容也(2)，渊渊乎其不可测也(3)。形德仁义，神之未也(4)，非至人孰能定之 (5)！ 夫至人有世(6)，不亦大乎，而不足以为之累。天下奋棅而不与之偕(7)，审乎无假而不与利迁(8)，极物之真(9)，能守其本(10)，故外天地，遗万 物，而神未尝有所困也(11)。通乎道，合乎德，退仁义，宾礼乐(12)，至人之心肴所‘定矣。”
	　　[注释]
	　　(1)夫子：指老聃。不终：没有穷尽。不遗：没有遗漏，包含有“其大无外，其小无内”的极限论意义。
	　　(2)广广乎：博大空阔啊。
	　　(3)渊渊乎：幽深玄远啊。
	　　(4)形德：形体之属性功能，如耳能听，目能视，鼻能嗅等，皆是这些形体器官之德。神之末，精神之枝节未流。
	　　(5)至人：与大道合一，达到精神上绝对逍遥自由的人，是庄子追求的最高理想人格。定：区分判定。指对无为道体与其外在枝节未流的区分判定，非至人则不能作到。
	　　(6)有世：有天下，做天下之帝王。
	　　(7)奋棅：争夺统治权柄。棅，通柄，指治国治民之权力。
	　　(8)审：慎，无假：无虚假之纯真本性。
	　　(9)极：穷尽。真：物之本性。
	　　(10)本：虚静无为之天道也。
	　　(11)外天地：指至人行无为而治，任天下循性自治，至人不以为意，不加干预，不为牵累，有同于无，故称外天下。遗万物：遗忘万物的具体形象和存在，只持守其本，能如此则精神就不会受到困扰。
	　　(12)退：黜退，宾：同摈，抛奔。
	　　[译文]
	　　先生说：“道，言其大则没有穷尽，言其小则没有遗漏，故而万物不完备地包藏其中。博大空阔啊它无不包容，幽深玄远啊它不可测知，形体之功能属性和仁 义，都属精神之枝节末流，它与无为本体之区分没有至人谁能判定呵！至人治理天下，其责任不是很重大么！然而不足以为其牵累。天下人都在奋力争夺统治权柄， 而至人不与他们相同，审慎持守真性而不随外利引诱迁变，穷尽物之真性，持守其根本，故而把天地置之度外，遗忘万物，而精神未曾受到困扰。与大道相通，与道 德相合，黜退仁义，抛弃礼乐，至人之心就能有安定之所了。”
	　　世之所贵道者书也(1)，书不过语，语有贵也。语之所贵者，意也，意有所随(2)。意之所随者，不可以言传也，而世因贵言传书(3)。世虽贵之，我犹 不足贵也，为其贵非其贵也(4)。故视而可见者，形与色也；听而可闻者，名与声也。悲夫，世人以形色名声为足以得彼之情。夫形色名声果不足以得彼之情，则 知者不言，言者不知(5)，而世岂识之哉！桓公读书于堂上(6)，轮扁研轮于堂下(7)，释椎凿而上(8)，问桓公曰：“敢问，公之所读者何言邪？”公 曰：“圣人之言也。”曰：“圣人在乎？”公曰：“已死矣。”曰：“然则君之所读者，古人之糟魄已夫(9)！”桓公曰：“寡人读书，轮人安得议乎！有说则 可，无说则死(10)。”轮扁曰：“臣也以臣之事观之。研轮，徐则甘而不固(11)，疾则苦而不入(12)不徐不疾，得之于手而应于心(13)，口不能 言，有数存焉于其间(14)。臣不能以喻臣之子(15)，臣之子亦不能受之于臣，是以行年七十而老研轮。古之人与其不可传也死矣(16)，然则君之所读 者，古人之糟魄已夫！”
	　　[注释]
	　　(1)世人之所以尊贵于道，是根据书籍。
	　　(2)意有所随：意有所从出，有所从来之本。
	　　(3)贵言传书：看重语言，把它记录于书，传之后世。
	　　(4)贵非其贵：被珍贵的并不真正值得珍贵。庄子认为：世上所珍贵的只是记录在书上的语言，语言是表达意的，而意所从出之本又不可言说。语言既不能表达意之本，也就没有什么值得珍贵处。
	　　(5)知者不言，真正知晓大道的不言说。言者不知，讲说大道的不是真正知晓。庄子认为：因为道超越经验和理性，不能言说，只能玄观体悟。所以用语言表述出来的，并不是真正的道，表述者也
	　　不可能知晓道。在《知北游》中，知与无为畏、狂屈、黄帝的对话，形象他说明这个道理。
	　　(6)桓公：齐桓公，姜姓，名小白。春秋初期齐国君主，第一位诸侯伯
	　　(7)轮扁，造车轮的匠人，名扁。斫（Zhu6）：砍削。
	　　(8)释：放下，椎、凿：皆为制造车轮的工具。
	　　(9)糟魄：魄同粕。把书比喻为古人留下的糟粕，并不是真正可贵的东西。
	　　(10)说：理由。
	　　(11)此句为制轮之经验谈，因年代久远，其具体情形已不易搞清楚。大体是讲车轮各部件制作安装要恰到好处，不松不紧，而如何掌握好，全靠主观体验，无法讲出来。徐：缓。甘：滑动。字面意义是说将各部件组装起来，如果松缓，就会滑动而不牢固。
	　　(12)疾：紧。苦：滞涩。过紧就会滞涩而难以安装。
	　　(13)手上作的与心里想的相应合。
	　　(14)数：同木，技艺。
	　　(15)喻：晓喻，说明。
	　　(16)不可传：不能用语言传授于人，只能自行体会的东西。如轮扁所讲的斫轮之术。
	　　[译文]
	　　世人之所以尊贵于道，是根据书上的记载，而书上所记载的不过是言语，言语有其可贵之处。言语之可贵处在于达意，而意有所从出之本。意所从出之本，是不 可以用语言相传授的，而世人却看重语言，把它记载于书而流传。世人虽珍贵它，我还是认为它不足珍贵，因为那被珍贵的并不真正值得珍贵。故而，用眼睛可以看 得见的，是形状与颜色；用耳可以听得到的，是名称与声音。可悲呀！世人以为得到形状颜色名称声音就足以获得其真实本性。依据形状颜色名称声音确实不足得到 其真实本性，所以，真正知晓的人并不言说，讲说的人并不是真知，而世人又怎能懂得啊！齐桓公在堂上读书，轮扁在堂下砍削制造车轮，他放下椎凿等工具走上堂 来，问齐桓公道：“请问，主公所读书是何人之言？”桓公说：“是圣人之言。”又问：“圣人还在世吗？”桓公说：“已经死去了。”轮扁说：“那么您所读的 书，不过是古人弃下之糟粕罢了！”桓公说：“我读书，制轮匠人怎么可以议论！能说出道理还可，说不出就处死。”轮扁说：“我是从我做的事观察出这番道理 的。砍削车轮，动作徐缓就会使车轮松动而不牢固，动作急剧就会使车轮部件滞涩而安装不进，动作不快不慢，就能使手上做的与心里想的相应，这种得心应手的感 受，口无法说出来，有技艺存在其中。我不能把它讲给我的儿子，我的儿子也不能从我这里接受过去，所以我已七十岁还在所轮。古代人和他们不可言传的东西都死 去了，然而您所读的书，不过是古人留下的糟粕罢了。”’


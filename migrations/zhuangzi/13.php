	　　(7)经：不变之常规、常法。
	　　(8)胶胶：粘台在一起不能解开。扰扰：纷乱不宁。尧听了舜的话受到启示，觉得自己的用心是多余的，不如静默无为，复归天德。
	　　(9)天之合，与天道相合。
	　　(10)天地而已矣：象天地那样虚静无为就是了。

	　　(3)废：抛弃，苦：忧劳。苦死者：对死者表示哀痛和抚慰。
	　　(4)嘉：善，亲爱之意。孺子：小孩。哀：怜悯。
	　　(5)未大：不算弘大。因为尧所讲皆有心而为，所及有限，故其心不算弘大。

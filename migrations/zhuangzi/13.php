	　　孔子西藏书于周室(1)，子路谋曰：“由闻周之征藏史有老聃者(2)，免而归居(3)，夫子欲藏书，则试往因焉(4)。”孔子曰：“善。”往见老聃而 老聃不许，于是繙十二经以说(5)。老聃中其说(6)，曰：“大谩，愿闻其要(7)。”孔子曰：“要在仁义。”老聘曰：“请问，仁义，人之性邪？”孔子 曰：“然，君子不仁则不成，不义则不生(8)。仁义，真人之性也，又将奚为矣(9)？”老聘曰：“请问何谓仁义？”孔子曰：“中心物恺(10)，兼爱无 私，此仁义之情也。”老聃曰：“意，几乎后言(11)！夫兼爱，不亦迂乎(12)！无私焉，乃私也(13)。夫子若欲使天下无失其牧乎(14)？则天地固 有常矣，日月固有明矣，星辰固有列矣，禽兽固有群矣，树木固有立矣(15)。夫子亦放德而行(16)，循道而趋，已至矣(17)！又何偈偈乎揭仁义 (18)，若击鼓而求亡子焉(19)？意，夫子乱人之性也。”

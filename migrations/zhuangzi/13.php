	　　天道无为之本君主掌握，政事礼法之未群臣执行；君主在上总其纲要，群臣在下行其细目。军队武器的动用，是道德之末流；赏罚利害之推行，五种刑法之设 立，是教化之未流；五礼之法，长度计算，名实比较审核，是治道之未流；用钟鼓奏出乐曲，用鸟羽兽毛装饰舞者，是乐之未流；哭祭丧服，各有等次，是哀悼之未 流。这五类未流枝节之事，必须待精神、心智运动，然后随之而动。五种末流枝节之学，古代就有，但不把它放在首要地位。君在先而臣从属，父在先而子从属，兄 在先而弟从属，年长者在先而年幼者从属，男人在先而女人从属，丈夫在先而妻子从属。天地之运行，有上下先后区分，故取而效法之。天在上地在下，是神明确定 的地位；春夏在先，秋冬在后，是四时之顺序；万物化生，萌生后区分为各种形状，再由兴盛转而为衰杀，是变化流行也。天地之道最为神妙莫测，还有上下先后之 顺序，何况是人道呢！宗庙祭把崇尚血缘之亲，朝廷崇尚高爵位，乡里间尊敬年长者，治事崇尚贤能，这是大道的先后次第。讲论道而不合道之第，不是真正的道； 讲述的不是真正的道，又从哪里去得道呢！
	　　所以古时明大道之人，先明天道而把道德放在其次，道德既明则把仁义放在其次，仁义既明则把职责放在其次，职责既明则把名实放在其次，名实既明则把因职 授事放在其次，因职授事既明则把推究省察放在其次，推究省察既明则把是非放在其次，是非既明则把赏罚放在其次，赏罚既明则愚笨的与聪明的都安排合宜，尊贵 者与低贱者各就其位，仁厚的贤达的和不成才的都依据实际作了安置。按其能加以区分，由其名而责其实。用这一套来服事君主，畜养下民，治理万物，修养自身， 就会不用智谋，复归于虚静无为之天道。这就叫作太平，是治道之极致。古书上说：“有形有名。”形名之区分，古人就有的，只是不放在首要地位。古代谈论大道 的人，经历五个层次的演绎推理，形名辨析可列举出来，九次演绎推理，赏罚被讲说出来。急剧匆忙去讲说形名问题，就不知道它之所本；匆忙讲述赏罚问题，就不 知道它之所始。违背道去讲，抵触道去说，只能为人所治，怎么能治理别人！匆忙讲说形名赏罚的人，他们只知治世的具体方法、手段，并不真正懂得治世之道。这 样的人可用于为天下事奔波劳碌，不足让天下自己治理自己。这就是言辩之士，只具一孔之见的人。五礼之法，长度计算，名实比较审核，古代就有。这是臣用以事 奉君的，不是君用以畜养臣民的。
	　　昔者舜问于尧曰：“天王之用心何如(1)？”尧由：“吾不敖无告(2)，不废穷民，苦死者(3)，嘉孺子而哀妇人(4)。此吾所以用心已。”舜曰： “美则美矣，而未大也(5)。”尧曰：“然则何如？”舜曰：“天德而出宁(6)，日月照而四时行，若昼夜之有经(7)，云行而雨施矣。”尧曰：“胶胶扰扰 乎(8)！子，天之合也(9)；我，人之合也。”夫天地者，古之资大也，而黄帝尧舜之所共美也。故古之王天下者，奚为哉？天地而已矣(10)。

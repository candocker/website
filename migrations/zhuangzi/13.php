	　　士成绮见老子而问曰(1)：“吾闻夫子圣人也，吾固不辞远道而来愿见，百舍重趼而不敢息(2)。今吾观子，非圣人也。鼠壤有余蔬而弃妹(3)，不仁 也！生熟不尽于前(4)，而积敛无崖(5)。”老子漠然不应(6)。士成绮明日复见，曰：“昔者吾有刺干子(7)，今吾心正却矣，何故也？”老子曰：“夫 巧知神圣之人，吾自以为脱焉(8)。昔者子呼我牛也而谓之牛，呼我马也而谓之马。苟有其实(9)，人与之名而弗受，再受其殃。吾服也恒服(10)，吾非以 服有服(11)。”士成绮雁行避影(12)，履行遂进(13)，而问修身若何？老子曰：“而容崖然(14)，而目冲然(15)，而颡頯然(16)，而口阚 然(17)，而状义然(18)，似系马而止也(19)，动而持(20)，发也机(21)，察而审(22)，知巧而睹于泰(23)，凡以为不信(24)，边 竟有人焉，其名为窃(25)。”

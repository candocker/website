
	　　[题解]
	　　《在宥》篇讲，“无为而尊者，天道也；有为而累者，人道也；主者，天道也；臣者，人道也。”本篇基本上是对这一思想的论述与发挥，从天道与人道关系方面阐述庄子的政治思想。全篇可分为七段
	　　第一段，阐述天道虚静无为，与圣道、帝道相通。能以虚静无为为宗本，则可“推于天地，通于万物”，得“天乐”，与天相合。第二段，从“夫帝王之德”至 “非上之所以畜下也”一大段，提出君道无为，臣道有为主帐，认为道德仁义、形名赏罚等，以及上下尊卑等级，都属人道，它与天道是一致的，表现为本末君臣关 系，也是治所需要的。其说近于黄老，有人以为后学所加。第三段，以尧舜对话形式，表达天德无为，如日月照耀，四时运行，昼夜更替，云，行雨施一样，是一种 自然运行过程。帝王应去掉世俗的粘滞缠绕，循性无为，效法天德。第四段，老子批评孔子倡导仁义；只会扰乱人性。天地万物是自然有序的，人只要“放德而行， 循道而趋”，对外界不加干扰，就自会实现理想境界。第五段，通过老子批评士成绩的言行，讲述得道之人，已经摆脱对神圣智巧的追求，不受外界毁誉之影响，保 持心性与行为仪容的恒常统一。第六段，阐述道无所不包又幽深莫测，圣人体道治世，“外天地，遗万物”，退仁义，弃礼乐，持守本真而内心安定。第七段，文字 语言皆为糟粕，真意不可言传。道是超越形色名声的，不在文字语言之中，讲说于人的都不是真道，真道不靠言论，只能玄观体悟。
	　　天道运而无所积(1)，故万物成；帝道运而无所积，故天下归(2);圣道运而无所积，故海内服。明于天，通于圣，六通四辟于帝王之德者(3)，其自为 也，昧然无不静者矣(4)。圣人之静也，非曰静也善，故静也；万物无足以挠心者(5)，故静也。水静则明烛须眉(6)，平中准(7)，大匠取法焉(8)。 水静犹明，而况精神！圣人之心静乎！天地之鉴也(9)，万物之镜也。夫虚静恬淡寂漠无为者，天地之平而道德之至，故帝王圣人休焉(10)。休则虚，虚则 实，实则伦矣(11)。虚则静，静则动，动则得矣(12)。静则无为，无为也，则任事者责矣(13)。无为则俞俞(14)，俞俞者忧患不能处(15)，年 寿长矣。夫虚静恬淡寂漠无为者，万物之本也。明此以南乡(16)，尧之为君也；明此以北面，舜之为臣也。以此处上，帝王天子之德也；以此处下，玄圣素上之 道也(17)。以此退居而闲游，江海山林之士服(18);以此进为而抚世(19),则功大名显而天下一也。静而圣，动而王(20)，无为也而尊，朴素而天 下莫能与之争美。夫明白于天地之德者，此之谓大本大宗(21)，与天和者也。所以均调天下(22)，与人和者也。与人和者，谓之人乐；与天和者，谓之天 乐。”庄子曰：“吾师乎(23)!吾师乎！繁万物而不为戾(24)，泽及万世而不为仁，长于上古而不为寿(25)，覆载天地。刻雕众形而不为巧(26)， 此之谓天乐。故曰，知天乐者，其生也天行(27)，其死也物化(28)，静而与阴同德，动而与阳同波(29).故知天乐者，无天怨，无人非，无物累，无鬼 责。故曰：“其动也天(30),其静也地，一心定而王天下(31); 其鬼不祟(32),其魂不疲，一心定而万物服。言以虚静推于天地，通于万物，此之谓天乐。天乐者，圣人之心以畜天下也(33)。”
	　　[注释]
	　　(1)天道：与人道相对，是中国古代含义宽泛的皙学范畴，一些唯物主义哲学家把它归结为天象运行，四时更迭，风雨寒暑等变化规律，与人事无涉。一些唯 心主义哲学家则认为，天道是神意的体现，并与人事相互感应，庄子认为天道是自然界无意识无目的运行，是无欲无为的。积：停滞。
	　　(2)帝道：帝工之道。与后面的圣道同效法天道，二者区别在于，帝是有位的，掌握着国家的统治权力，可以直接推行其道。圣则是无位的素王，通过他的学说和道德发生影响，归：归附。
	　　(3)六通：四方上下无不畅通。四辟：春夏秋冬无时不开辟，六通四辟：形容于帝王之道全面通晓。
	　　(4)昧然：暗昧不觉。
	　　(5)饶：通挠，搅乱也。
	　　(6)烛：照。
	　　(7)平中准，水面平静，与水准仪器相符合。中（Zhong），符合。准，测水平仪器。
	　　(8)大匠取法：高明的木匠师傅效法它作成测量平面的器具。
	　　(9)鉴：镜。
	　　(10)休：栖止。这句的意思为：虚静恬淡，寂寞无为是帝王圣人使心栖止之所。
	　　(11)虚则实：心虚静能鉴照天地万物，故而充实。实则伦：充实中包含一切条理秩序。
	　　(12)静则动，天道之动静不是僵死不变的，而是不断转化推移的。如《齐物论》“方生方死，方死方生，方可方不可，方不可方可。”讲生死、可不可之转化推移，与此同理，动则得：天道不停止运动，万物与之吻合同步，则得其所宜。
	　　(13)责：尽职责。
	　　(14)俞俞：从容自如的样子。
	　　(15)不能处：处，止也。得无为之道者，忧患不能留止于心。
	　　(16)南乡：即南向，面南背北，为古代君主听居之位。
	　　(17)玄圣素王：得无为之道，为天下人敬仰而又未处帝王之位的人，如老耿及庄子虚拟的一些得道者。
	　　(18)江海山林之士：隐居在海岛深山的隐士。
	　　(19)进为：出仕作官，为帝王辅佐，如伊尹吕望之类。抚世：安抚治理世人。
	　　(20)静而圣：保持自身虚静无为则为圣人，动而王：无欲无求，顺夭道而动则为帝王。
	　　(21)大本大宗：指天地万物的根本性质和产生本原。
	　　(22)均调：均平协调。
	　　(23)师：比喻天道，庄子以天道为师，重复申说，表示衷心赞叹。
	　　(24)■（xiè)碎。戾：暴戾。
	　　(25)长：年长。寿：长寿。道是无始无终的，永恒的，说它比上古还要年长，只是一个比喻。
	　　(26)刻雕众形：比喻道创生万物的多种形态，好象匠人雕刻出各种物形。
	　　(27)天行：天道之运行。
	　　(28)物化：物象之幻化，认为人死不过是由一种物幻化成另一砷物，就象庄周梦中化成蝴蝶，醒来又成庄周一佯。
	　　(29)波：扩展。
	　　(30)其动也天：其动时无心无为，循性自如，如同夭道之运行。
	　　(31)这句是说，持守心之虚静无为，就可以为天下王。
	　　(32)祟，祸。其鬼不祟：其为助词，表强调义，强调鬼神也不能带来灾祸。
	　　(33)以：用。畜，养。天乐指圣人执守虚静无为，达到的与夭地为一，与变化同体的道德境界，圣人将其推行于天地万物，用它来畜养天地万物。
	　　[译文]
	　　天道运行而不停滞，故而万物得以生成；帝玉之道运行而下停滞，故而天下之民都来归服；圣人之道运行而不停滞，故而海内之民敬服。明于天道，通于圣道， 于帝王之德无不通达的人，任物循性自为，对这一切暗昧不觉而执守虚静之心。圣人执守虚静，不是因为虚静好，才去作的。而是万物不足以搅乱他的心，所以心虚 静，水平静就可以清楚照见人的胡须眉毛，其平面符合水准队器，高明的木匠师傅就是取法于此而造成水平仪器的。水平静还能如此明察，何况是人的精神呢！圣人 之心虚静，可以成为大地的镜子，万物的镜子。虚静、恬淡、寂寞、无为，就是天地之平静和道德的最高境界，因此，帝王圣人都栖心于此。心休止则虚静，虚静则 能鉴照万物而充实，充实中包含万物之条理秩序，心空虚即得平静，平静又转化为运动，运动与天道合则万物各有所得。虚静即能无为，君无为，则百官各尽职责。 行无为之道则能从容自如，从容自如的人，忧患不能留止于心，所以能长寿。虚静、恬淡、寂寞、无为，是万物的本性。明晓此道而南面为君，就能成为尧一样的 君；明晓此道而北面为臣，就能成为舜一样的臣。以此道处上位，就成为帝王天子的最高德行；以此道处下位，就成为玄圣素王的正道。以此道退隐闲游，海岛山林 之隐士都会敬服；以此道出仕作官，辅佐帝王安抚治理人民，则能建大功显名声而使天下统一，虚静而为圣人，顺天动而为帝王，无为而受尊崇，朴素之美天下没有 能与之相争的。明白无为是天地之本性，这就是把握了万物的根本性质和产生根源，就是与天道相合了，因此就能均平协调天下之事，而与人和谐融洽。与人和谐融 洽，称之为人乐；与天相合，称之为天乐。庄子说：“我的老师呀！我的老师呀：打碎万物不算作暴戾，恩泽及千万代不叫作仁慈，比上古更年长不称为长春，覆盖 承载天地、创生方物的多种形态而不称为巧妙，这就叫作天乐。所以说，知晓天乐的人，其生与天道一同运行，其死为物相幻化。虚静时与阴具有同一德性，运动时 与阳一起扩散传播。故而知晓天乐的人，不报怨夭，不非难人，不受外物牵累，不责备鬼神。所以说，这样的人动时如天之运行无滞，静时如地之虚静充实，其心安 定而为天下王；鬼神不会带给灾祸，精神也不会疲劳，其心安定而万物顺服。这些话都是说把虚静无为推行于天地，畅通于万物，这就叫天乐。天乐，是圣人用来畜 养天下的。”
	　　夫帝王之德，以天地为宗(1)，以道德为主，以无为力常。无为也，则用天下而有余(2)；有为也，则为天下用而不足(3)，故古之人贵夫无为也。上无 为也，下亦无为也，是下与上同德，下与上同德则不臣(4)。下有为也，上亦有力也，是上与下同道，上与下同道则不主(5)。上必无为而用天下，下必有为为 天下用，此不易之道也(6)故古之王天下者，知虽落天地，不自虑也(7)，辩虽彫万物(8),不自说也；能虽穷海内(9),不自为也。天不产而万物化，地 不长而万物育(10)，帝王无为而天下功。故曰莫神于天，莫富于地，莫大于帝王。故曰帝王之德配天地。此乘天地，驰万物(11)，而用人群之道也。
	　　[注释]
	　　(1)宗：本。天地覆载万物而无心，帝王则以之为本。
	　　(2)用天下而有余：对万物的变化生灭，社会的治乱兴衰，君主下加干预，任其自然，所以闲暇无事。
	　　(3)为天下用而不足：天下事是无穷尽的，虽弃波劳苦，砷精竭虑去作，仍然不能作完，故而力不足。
	　　(4)下臣，臣无为则丧失为臣之德。这里主张君德无为，臣道有为，二言下可相滥，实际是主张按干道各司其职。
	　　(5)不主：君有为则失去君主之德。
	　　(6)不易：不变。不管世道如何变迁，此道永不改变。
	　　(7)知：同智，智慧，落：通络，包括，包笼之意。不自虑：不自行代天思虑。
	　　(8)彫：钟泰《庄子发微》以为“彫藉为周”，可从。辩彫万物：言其辩论可以周遍万物。只是一种形容，实际上下可能达到。《齐物论》就讲：“言辩而下周”。
	　　(9)能虽穷海内：虽穷尽四海之内也找不出如此多能之人。
	　　(10)下产不长：意为天地无意于万物的产生和生长，万物的产生和长成皆出于自然。
	　　(11)乘，驾驭，驰：驱使。
	　　[译文]
	　　帝王之德性，以天地为宗本，以道德为主宰，以无为力常法。无为，任天下自行治理则有余暇；有力，力天下疲于奔命则力不足。所以古人治天下贵无为之道， 君上无为，臣下也无为，是臣下与君上有同一德性，臣下与君上有同一德性则丧失为臣之德；臣下有为，君上也有为，是君上与臣下行道同一，君上与臣下行道同一 则不成其为君主。君主在上必行无为之道而使天下自行治理，群臣在下必须有力去为天下作事，这是永不改变之道。所以古时为天下之王者，其智慧虽能包笼天地， 也不自行代天思虑；其知辩虽能周遍万物，也不自己去言说；其能力虽然海内无比，也不自去作为。天无意于生产而万物化生，地无意于生长而万物长成，帝王无力 而天下事自行成功，所以说没有比天更神妙莫测，没有比地更富有，没有比帝王之德更博大。所以说帝工之德与天地相配合。这就是驾驭天地，驱使万物，任用万民 之道啊！
	　　本在于上，未在于下(1);要在于主，详在于臣(2)。三军五兵之运，德之未也(3);赏罚利害，五刑之辟(4)，教之未也；礼法度数(5)，形名比 详(6)，治之未也；钟鼓之音，羽旄之容(7)，乐之未也；哭泣衰绖(8)，隆杀之服(9)，哀之未也。此五未者，须精神之运，心术之动(10)，然后从 之者也。未学者，古人有之，而非所以先也。君先而臣从，父先而子从，兄先而弟从，长先而少从，男先而女从，夫先而妇从。夫尊卑先后，天地之行也(11), 故圣人取象焉(12)。天尊地卑，神明之位也(13);春夏先，秋冬后，四时之序也；万物化作，萌区有状(14)，盛衰之杀，变化之流也(15)。夫天地 至神，而有尊卑先后之序，而况人道乎！宗庙尚亲，朝廷尚尊，乡党尚齿(16),行事尚贤，大道之序也。”语道而非其序者，非其道也。语道而非其道者，安取 道(17)! 是故古之明大道者，先明天而道德次之，道德已明而仁义次之，仁义已明而分守次之(18)，分守已明而形名次之，形名已明而因任次之(19)，因任已明而原 省次之(20)，原省已明而是非次之，是非已明而赏罚次之，赏罚已明而愚知处宜(21),贵贱履位(22)，仁贤不肖袭情(23)。必分其能，必由其名 (24)。以此事上，以此畜下(25),以此治物，以此修身，知谋不用，必归其天(26)。此之谓大平(27)，治之至也。故书曰：“有形有名。”形名 者，古人有之(28),而非所以先也。古之语大道者，五变而形名可举(29)，九变而赏罚可言也(30)。骤而语形名(31),不知其本也；骤而语赏罚， 不知其始也，倒道而言(32)，迁道而说者(33)，人之所治也(34)，安能治人！骤而语形名赏罚，此有知治之具(35)非知治之道。可用于天下，不足 以用天下(36)，此之谓辩士，一曲之人也(37)。礼法数度，形名比详，古人有之，此下之所以事上，非上之所以畜下也。
	　　[注释]
	　　(1)本末：中国古代哲学重要范畴，包含本体与现象，主要与次要，根本与从属诸方面及其相互关系的论述辨析，先秦时期多言本，还未将本末对举作系统阐 述，汉与魏晋时期则有较多讨论。在中国传统观念中，则以农为本，以工商为未。此处本未有特定意义，本为根本，指天道无为；未为枝节，指人事之政事礼法等。 上为君主，下为群臣，意即天道无为是根本，由在上之君主掌握。政事礼法是枝节，由群臣执行。
	　　(2)要：纲要，机要。详，细目、细节。这句话意思为：君主行的是无为之道，只总概要就够了，细节细目要臣去作。
	　　(3)三军：泛指军队。五兵：五种兵器，具体所指说法不一，通行说法指矛、戟、钺、楯。弓矢。运：运用。
	　　(4)五刑：墨、剿、刚、宫、大辟。辟：法。
	　　(5)礼法：吉、凶、军、宾、嘉五礼所遵行的法度。度：计量长短之标准，如丈尺之类。数：数字计算。
	　　(6)形名比详：即对事物之名实关系进行比较审核。形指事物，名力名、称，比为比较，详为审核、审定。
	　　(7)羽旄之容：用鸟羽、兽毛装饰歌舞者的服装、道具，以显示仪客华美。羽，鸟羽。姹，兽毛。
	　　(8)衰绖（cuidie）：衰，丧服，绖为用麻制作的腰带和冠带，皆为服丧时穿戴，根据生者与死者血缘亲疏关系，丧礼规定了相应的哭祭仪式和丧服规格，不可以相滥。
	　　(9)隆杀之服：隆，加隆、提升；杀为降等。丧服分斩衰、齐衰、大功、小功、绸麻五种，要根据本人与死者关系之亲疏，予以加隆或降等，确定其应当穿那种丧服。
	　　(10)心术：心智、心之能力。此句意为，精神心智是本，五未必须从属于本，不能把枝节当根本，本末倒置。
	　　(11)尊卑：上下也。言天地之运行有上下先后之分。
	　　(12)取象，取而效法。
	　　(13)言天在上，地在·已是神明安排之位置。
	　　(14)萌区有状：万物萌生后区分为各种形状。
	　　(15)由兴盛转而降为衰杀，是变化之流行。

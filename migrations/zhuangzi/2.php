	　　当晋国开始得到她的时候，哭得泪水湿透了衣襟；等她到了晋献公的王宫里，和国王睡在一张方正而安适的床上，同吃美味的牛羊猪狗肉时，才后悔当初不该哭 泣。我怎能知道死了不后悔当初不该贪生呢？梦中开怀畅饮，醒了之后却要痛哭流涕；梦中痛哭流涕，醒了又去狩猎取乐。当他正在梦中，不知他是在做梦，睡梦中 还占卜问他梦中之梦的吉凶，醒了之后才知道是在做梦。只有特别清醒的人才知道人生是一场大梦，而愚昧无知的人，自以为狼清醒，表现出明察一切的样子，觉得 他什么都知道。什么君主啊，什么臣子啊，太浅陋了！我看孔丘和你都在做梦，我说你们在做梦，我也在做梦。这些言论可以把它称为怪异的言论，也许万世之后会 遇到一位大圣人能了解这个道理，那也是旦暮相遇的偶然现象。即使我与你进行辩论，你胜了我，我没有胜你，你肯定对，我肯定错吗？我胜了你，你没有胜我，我 肯定对，你肯定错吗？是我们两个人有一方是对的，有一方是错的呢？还是我们双方都对，或者都错呢？我与你都不知道，别人本来就受到它的蒙蔽而暗淡不明。
	　　我们请谁来评判是非呢？假使请观点和你的观点相同的人来评判，他既然和你的观点相同了，又怎样评判呢？假使请观点和我的观点相同的人来评判，他既然和 我的观点相同了，又怎么能评判呢？假使请观点和你我观点都不同的人来怦判，他既然与我和你的观点都不同，又怎么能评判呢？假使请观点和你我都相同的人来评 判，他既然与我和你的观点都相同，又怎么能怦判呢？那么，我和你和其他别人都不评定谁是谁非了，还等待谁来评判呢？”“什么叫做混同于自然来调和一切是非 呢？”就是说：“有是就有不是，有对就有不对。是的如果真的是是的，那么，是的不同于不是的也就不须分辨了：那些辩论的言词化作声音而相互对立，因为不能 相互评判，所以就象没有对立一样，混同于自然之分，顺应着无穷的变化，从而享尽一生。对的果真是对的，那么，对的不同于不对的也就不须分辨了。忘掉生死岁 月，忘掉是非仁义，就能畅游于无穷的境界，这样也就把自己寄托在不能穷尽的境域了。”

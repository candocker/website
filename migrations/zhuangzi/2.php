	　　言论不是吹风，发表言论的人都有所说的内容，但他们的言论又都自以为得当而不能有定论。他们果真有这些言论呢？还是没有过这些言论呢？他们自以为自己 的言论不同于刚出蛋壳的小鸟叫声，到底是有分别呢，还是没有分别呢？道是怎么被隐蔽而有真伪的呢？言论是怎样被隐蔽而有是非的呢？道是无真伪的在什么地方 不存在呢？言论是无是非的在哪些方面有不可的呢？道的本质隐蔽在片面认识的后面，言论的性质隐蔽在花言巧语之中，因向才有儒墨显学的是非之争，他们都各自 肯定对方之所非，而非议对方之所是，如要肯定对方的所非而非议对方的所是，则不如以空明的心境去反映事物的实情。宇宙间的事物没有不是彼的，也没有不是此 的，从彼方看不见此方，从此方来看就知道了。所以说，波方是出于此方，此方也依存于彼方。彼此是相互并存的。虽然如此，生中有死的因素而向死转化，死中有 生的因素而向生转化，肯定中有否定因素而向否定转化，否定中有肯定因素而向肯定转化；由是而得非，由非而得是，因此，圣人不经由是非之途而只是如实地反映 自然，也就是因任自然这条道理。此也是彼，彼也是此。彼有一个是非，此也有一个是非，果真有彼此之分吗，果真无彼此之分吗？彼此都没有它的对立面，这就是 物通为一的规律。符合道的规律才能得到它的运转的圆机，以顺应无有穷尽的发展变化。是的发展变化是无穷尽的，非的发展变化也是无穷尽的。所以说不如以空明 的心境去反映事物的实情。

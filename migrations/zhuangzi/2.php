	　　用指的概念来说明具体的指不是指，不如用不是指的概念来说明一般的指不是具体的指。用马的概念来说明具体的马不是马，不如用不是马的概念来说明一般的 马不是具体的马。其实天地之大就是一指，万物千差万别不过就是一马。肯定自有肯定的道理，否定自有否定的道理。道路是人走出来的，事物的名称是人叫出来 的，怎样才算对的？对的就是对的；怎样算是不对的？不对的就是不对的。怎样算是肯定？肯定就是肯定；怎样算是否定？否定就是否定。万物各有其存在的依据， 万物各有其合理性，没有什么事物是不对的，没有什么事务是不可肯定的。所以为了说明这个道理，可举草茎和大柱子小大相同，丑厉和西施丑美一样，千奇百怪一 切情态，从道的观点来看，都是齐一无别的。万物总体的分就是众体的成，新事物的成又是旧事物的毁。一切事物没有成与毁的分别，还是把它们看成是齐一的。只 有这样，通达的人才会通晓万物齐一的道理。因此，不用成毁的观点看问题，而托付于循环往复的观点看问题，按循环往复的变化行事，就是无用之用，就无所不 通，无所不通，就无所不得。达到满意而有所得也就差不多了。听任自然吧，把万物看成齐一而不去了解它的所以然，这就叫做道，耗费自己的聪明才智才了解万物 齐一，和了解万物的本来面貌就是一样的，这两者没有什么差别，可以把它叫做“朝三”。什么叫“朝三”呢？养猕猴的老人在分给猕猴橡子时说：“早晨三升而晚 上四升。”所有的猴子都非常愤怒。老人又说：“那么就早晨四升而晚上三升吧。”所有的猴子都非常喜悦。其实名义和实际都没有什么亏损，然而却使猴子喜怒不 同，这就是顺应猴子的心理作用罢了。所以，圣人调和是非而不去争论，这就是“因是”、“以明”两个轮子缺一不能行走的道理。

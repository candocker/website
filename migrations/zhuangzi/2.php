	　　[译文]
	　　过去庄周梦见自己变为蝴蝶，栩栩如生飞舞自得的蝴蝶，自己为适合心意而感到愉快。竟然忘掉自己是庄周了。倾刻间觉醒了，就惊喜的意识到自己仍然是庄周。不知道是庄周在梦中变成了蝴蝶呢，还是蝴蝶在梦中变为庄周呢？庄周和蝴蝶毕竟是有区别的。这种物我的变化就叫做物化。


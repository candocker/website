	　　(1)指句：援引公孙龙《指物论》的观点阐发作者的理论，公孙龙《指物论》：“物莫非指，而指非指”，前指为一般概念、共性、观念的体现，后指为观念 自身，意思是事物莫不是观念的体现，而体现观念的事物却不是观念本身。也就是说，观念是决定事物存在的，观念与事物是各自独立的。庄子“指句”的意思与其 说是“物莫非指，而指非指”推出物与指各自独立存在，倒不如说物指根本不是什么指，而是人的观念随意安排，推出的指物都不是具体存在的，不必认真对待。此 句用公式来说明，用A 来说明A 不是～A，不如以～A 来说明不是～A，即用此喻此之下同于彼，不如以彼喻此之不同于彼，也可以说以我来衡量他，不如以他来衡量我。喻：晓喻，晓导，明白告知。《礼·学记》： “可谓善喻矣”。《汉书·刑法志》“欲以晓喻众庶”皆作“说明”解，与本文同。
	　　(2)马句：也是援引公孙龙《白马论》中的“白马非马”的命题。意思是从马的概念出发，来说明具体的白马不是马，不如从具体的马出发来说明马的概念不 是具体的马。旧注多以为指马句皆为庄子对公孙龙的批判，实际并非如此。此二句是引用公孙龙的观点来阐明庄于自己的见解。公孙龙的“指非指”是说认识能力与 认识对象的不同；“马非马”是认识上的片面和全面观点有别。而庄子则更进一步，以为认识对象是由认识能力决定的，片面性是因全面性而存在的，从而主张去掉 那种以认识能力代替认识对象，以片面代替全面的见解，消除一般与个别的对立。所以庄子指马二句是在“无有”上立论，而并非批判公孙尤之说，以马喻马，前马 是一般概念，后马指具体的马。


	
	　　[题解]
	　　《列御寇》以人名篇。列御寇，郑人，道家学派的先驱者，人称列子，主张贵虚。全篇的主旨在于宣扬不可炫智于外而应养神于心，达到“天而不入”顺从自然，达到无用之用的境界。
	　　在“列御寇之齐”段中，庄子通过列子说明要达到自忘、忘我“虚而邀游”的思想境界。在“郑人缓也呻吟裘氏之地”段中，肯定了朱评漫的天而不人的无用之 用的观点，否定了郑人缓的人而不天的自以为是的思想。在“宋人有曹商者”段中，否定了曹商的追势得利的观点和孔于的“使民离实学伪”的观点，而肯定只有真 人才能达到“负乎内外之刑”。在“孔子日”段中，指出了处世的方法就是因顺自然，而不要着眼于人事。这是一种虚无主义的处世哲学。
	　　上述几方面思想观点是有内在联系的，并非象有人说的：各节间极其散乱意义不相关连。
	　　列御寇之齐(1)，中道而反(2)，遇伯昏音人(3)。伯昏瞀人曰：“奚方而反(4)？”曰：“吾惊焉(5)。”曰：“恶乎惊？”曰：“吾尝食于十浆 (6)，而五浆先馈(7)。”伯昏瞀人曰：“若是(8)，则汝何为惊已(9)？”曰：“夫内诚不解(10)，形谍成光(11)，以外镇人心(12)，使人 轻乎贵老(13)，而■其所患(14)。夫浆人特为食羹之货(15)，多余之赢(16)，其为利也薄，其为权也轻，而犹若是，而况于万乘之主乎(17)！ 身劳于国而知尽于事(18)，彼将任我以事而效我以功(19)，吾是以惊。”伯昏督人曰：“善哉观乎(20)！女处己(21)，人将保女矣(22)！”无 几何而往(23)，则户外之屡满矣(24)。伯昏瞀人北面而立民敦杖蹩之乎颐(26)，立有间(27)，不言而出。宾者以告列子(28)，民列子提履，跳 而走(29)，暨乎门(30)，曰：“先生既来，曾不发药乎(31)？”曰：“己矣，吾固告汝曰人将保汝，果保汝矣。非汝能使人保汝，而汝不能使人无保汝 也，而焉用之感豫出异也(32)！必且有感(33)，摇而本才(34)，又无谓也，与汝游者又莫汝告也(35)，彼所小言(36)，尽人毒也(37)。莫 觉莫悟(38)，何相孰也(39)！巧者劳而知者优，无能者无所求(40)，饱食而敖游(41)，泛若不系之舟(42)，虚而敖游者也(43)。”
	　　[注释]
	　　(1)列御寇：人名，亦作圄寇、圉寇，人称列子。郑国人，列子贵虚，为先秦道家学派的先驱，之：往，去。齐：齐国。
	　　(2)中道：中途。反：通返，返回。
	　　(3)伯昏音人：人名，楚国的贤人，隐者。《德充符》作伯鲁无人，《日子云》作伯昏无人，《列子·黄帝篇》作伯昏督人。
	　　(4)方，刚，才。作道术、方向、事，借为妨解实误。从上句“中道而反”和下句“吾惊焉”来看，解为为什么刚去就反回来为当。
	　　(5)惊：惊异。
	　　(6)浆：米汤，指卖米汤的店铺。十浆，十家浆铺。
	　　(7)五浆：五家浆铺。馈：馈赠，赠给。
	　　(8)若是：如此，这样。
	　　(9)己：通矣。
	　　(10)内诚：内心真诚。不解：有症结没有融化。
	　　(11)形谍：指没有融化的症结从外表上流露出来。谍，同深，泄。
	　　(12)镇：镇服。
	　　(13)馈，指爵位高。老，指年老。
	　　(14)■（jī）：招致。
	　　(15)特，只是。
	　　(16)赢：赢利，赚钱。
	　　(17)万乘之主：古代皇帝。
	　　(18)劳：操劳。知：通智。
	　　(19)彼：指国君。效，责效。
	　　(20)观：观察。
	　　(21)己：旧读己实误，当为已，语助词，作止解。
	　　(22)保，归附，依附。
	　　(23)无几何：没多久，不几天。
	　　(24)屦（jù），麻葛鞋。
	　　(25)北面：面北。
	　　(26)敦：作顿。敦杖：以杖顿地。蹙之乎颐：拄他的面夹。蹙，迫。
	　　(27)立有间：站一会。
	　　(28)宾（bīn）：同傧，傧相，接引客人的人员。
	　　(29)跌（xiǎn）：光着脚。
	　　(30)暨，及，乎，于。
	　　(31)曾：乃，发药，比喻治病救人的言论。
	　　(32)而：通尔，你，用：因。豫：通偷，愉快。
	　　(33)必且：必将。
	　　(34)摇：动，摇动。而：你。才：一作性。
	　　(35)告（gù）：上告下。莫汝告：莫告汝。
	　　(36)小言：项碎的言论，作甜言蜜语解亦通。
	　　(37)尽入毒：尽是害人的东西。
	　　(38)莫，不。
	　　(39)孰：古熟字，成。
	　　(40)无能：无所能而能，指无为而得道。
	　　(41)敖游：这游，不受外物的束缚，自由自在地游荡于虚无的境界。
	　　(42)汛：飘流不定，漫无目的。
	　　(43)虚：内心空虚无目的，指无应无不应。
	　　[译文]
	　　列御寇去齐国，中途返回来，遇到伯昏督人。伯昏瞀人说：“为什么刚去就返回来呢？”列御寇说：“我很惊异。”伯昏瞀人说：“为什么惊异？”列御寇说： “吾曾在十家浆铺饮浆，而有五家先馈赠。”伯昏音人说：“如此，你为什么惊异？”列御寇说：“内心真诚而有症结不化，由外表流露出来形成光采，以此镇服人 心，使人轻视权贵和老人，从而招致祸患。卖浆人只是做些饮食买卖，残余的赢利，得的利润甚少，所得权势也轻微，还要如此，何况是万乘之军的君主呢？身躯操 劳于国事而智慧耗尽干政事，他将委任我以政事而要我达成功效，因此我感到惊异。”伯昏瞀人说：“观察的很好呀！你在家等着吧，人们会归附你了！”没过几天 又到列子住处，门外的鞋摆满了。伯昏瞀人面北站着，手杖顿地拄着面夹，站了一会儿，没有说话就走了。接待宾客的人告诉列子，列子提着鞋，光着脚走出来，到 门口，说：“先生既然来了，却不说点药石之言吗？”回答说：“算了吧，我本来告诉你说人们要归附你，果然归附你了。不是你能使人归附你，而是你不能使人归 附你，你何必因为这种事感到愉快而显出与众不同呢！一定要使人们感动，就会动摇你的本性，又是无所谓的事。与你一起交游的人又不告诉你，他们所说的项碎的 言论，都是害人的。不相互提高觉悟，又怎能相互成熟呢！技巧的人操劳而智慧的人忧虑，无所能而能的人无所追求，吃饱饭的人而不受外物拘束地邀游，飘飘然象 没有拴住的船只，内心空虚而邀游。”
	　　郑人缓也(1)，呻吟裘氏之地(2)，抵三年而缓为儒(3)，河润九里(4)，泽及三族(5)，使其弟墨(6)。儒墨相与辩，其父助翟(7)。十年而 缓自杀。其父梦之曰(8)：“使而子为墨者予也(9)。阖胡尝视其良(10)，既为秋柏之实矣(11)？” 夫造物者之报人也(12)，不报其人而报其人之天(13)。彼故使彼(14)。夫人以己为有以异于人(15)，以贱其亲(16)。齐人之井饮者相摔也 (17)。故曰：‘今之世皆缓也自是(18)。’有德者以不知也(19)，而况有道者乎！古者谓之遁天之刑(20)。圣人安其所安(21)，不安其所不安 (22)；众人安其所不安(23)，不安其所安。
	　　[注释]
	　　(1)缓：郑国一个人的名字。
	　　(2)呻吟，微弱的诵读声。裘氏：郑地名。
	　　(3)抵（zhī）：适，正好。为儒：学儒而成为儒。
	　　(4)河：河水。润：滋润，灌溉，九：多数，概数，非指八、九之九。
	　　(5)泽：恩泽。三族：父族、母族、妻族。
	　　(6)墨：学墨家的学说。
	　　(7)翟（dí）：本为墨子名，此以墨子名做缓之弟弟名字。有双关的意思。
	　　(8)之：代缓。之曰：指缓托梦说。
	　　(9)而，你。予：我，指绶自己。
	　　(10)阖胡：何不。尝：试。其：缓目宿。良：（làng）：一作壤，坟墓。
	　　(11)秋：借作揪。楸柏：皆为良材。实：指学术的成就。
	　　(12)报：报答，对待，给予。
	　　(13)天：天性，自然本性。
	　　(14)彼：他，指缓弟。彼：指成墨者的事。
	　　(15)夫人：指缓。异于人：不同于别人。
	　　(16)贱其亲：指责他的父亲，贱侮他的父亲。
	　　(17)齐人：齐国的民众。井饮，喝井水。相捽（zu6）：抓着头发互相殴打。
	　　(18)自是，自以为是。旧注将“自是”断下句，非是。
	　　(19)不知：不知自以为是，即不知有德。
	　　(20)遁夭之刑：违背自然的刑罚。
	　　(21)安其所安：安于自然无为。
	　　(22)不安其所不安：不安于人为。
	　　(23)众人：一般人，普通人。
	　　[译文]
	　　郑国有一个名叫缓的人，在裘氏的地方读书。正好三年便成为儒者，象河水一样滋润九里，恩泽三族，又让他的弟弟学墨。兄弟二人以儒墨观点相互辩论，他父 亲帮助翟。十年后缓气愤自杀了。给他父亲托梦说：“让你儿子成为墨者的是我。为什么你不来看看我的坟墓，我已经变成揪柏结成果实了。造物者给予人的，不是 给予人力而是给予人的天然本性。翟的夭性使他成为墨者。缓以为自己与众不同而贱侮他父亲，就象齐民凿井以为造泉而相互殴打一样。所以说，‘现在社会上的人 都象缓那样自以为是。’有德的人不认为有德，何况是有道的人呢！古时候认为象缓这样自以为是的人是违背自然刑罚的人。圣人安于自然天性，不安于人为自是； 一般人安他所不安的人为自是，不安于他所当安的自然天性。
	　　庄子曰：“知道易(1)，勿言难(2)。知而不言，所以之天也(3)；知而言之，所以之人也；古之人天而不人(4)。”朱评曼学屠龙于支离益(5)， 单干金之家(6)，三年技成而无所用其巧(7)。圣人以必不必(8)故无兵(9)；众人以不必必之(10)，故多兵；顺于兵(11)，故行有求(12)。 兵，恃之则亡(13)。小夫之知(14)，不离苞直竿犊(15)。敝精神乎蹇浅(16)，而欲兼济道物(17)，太一形虚(18)。若是者，迷惑于宇宙， 形累不知太初(19)。彼至人者，归精神乎无始(20)，而甘冥乎无何有之乡(21)。水流乎无形(22)，发泄乎太清(23)。悲哉乎汝为(24)，知 在毫毛(25)，而不知大宁(26)！
	　　[注释]
	　　(1)知：认识。
	　　(2)勿言难：默不作声而成之者困难。
	　　(3)之：向，往。之天：合于自然。
	　　(4)古之人：古时的至人、真人、圣人。天：天道自然。不人：不合人为。
	　　(5)朱泙（pēng）漫：人名。支离益：人名，复姓支离，名益。屠龙：喻为道。
	　　(6)单：借为殚，尽。家，指家产。
	　　(7)技：技术。巧：技巧。
	　　(8)必不必：必可用而不用。
	　　(9)兵：争。无兵：无事。
	　　(10)不必必之：不必可用而必用它。
	　　(11)顺：顺从，曲从。
	　　(12)行，行为。求：贪求。
	　　(13)恃：依靠。亡：亡失，不得，作丧亡、灭亡解实误。
	　　(14)小夫：指匹夫，一般世俗之人，知：同智
	　　(15)苞直：债赠人鱼肉用茅苇叶包着。竿：通简。竿犊：简犊，古书。
	　　(16)敝：消耗。蹇浅：浅陋，短浅。
	　　(17)兼：兼而有之。济：成。兼济道物：成道又成物。
	　　(18)太一形虚：指取形和虚而一贯，大一，一贯。
	　　(19)太初：指道的本体。
	　　(20)归：复，回。无始：万物还没产生的时代。
	　　(21)甘冥：甜睡，冥，通瞑，眠，无何有之乡：指虚无的境界，《逍遥游》有“何不树之于无何有之乡”。
	　　(22)水流：以水比喻道。水流乎无形：指水流是无所不到的。
	　　(23)太清：太虚清静无为的自然之道。
	　　(24)汝：指列子。旧注将“汝为”断在下句实误。
	　　(25)知：通智。毫毛：指小事。
	　　(26)大宁：大的宁静的境界。
	　　[译文]
	　　庄子说：“认识道容易，默不作声而成道困难，认识道而默不作声，才合于自然；认识道而说出来，这是合于人为。古时候的至人合于天道自然而下合于人道人 为。”朱泙漫跟支离益学屠龙，花尽了千金的家产，三年学成技术却无处使用这种技巧。圣人以必可用而不去用，所以没有争端；一般人以不必用而必去用它，所以 引起许多纷争；顺从于争端，所以行为有贪求。面对纷争，依靠它就会什么也得不到。世人的智慧，离不开苞直简犊，把精神消耗短浅的小事上，而想成道又成物， 一贯形虚。象这样，会为宇宙所迷惑，为形体劳累而不知道的本体。那种至人，把精神归属于万物没产生之前，而甜睡于虚无的境地。水流于无形，发泄于太虚清静 的自然。可悲啊列子所为，把智慧放在毫毛的小事上，而不知道大的宁静的境界。
	　　宋人有曹商者(1)，为宋王使秦(2)。其往也，得车数乘；王说之(3)，益车百乘(4)。反于宋(5)，见庄子曰：“夫处穷阎厄巷(6)，困窘织屡 (7)，槁项黄诚者(8)，商之所短也(9)；一悟万乘之主而从车百乘者(10)，商之所长也(11)。”庄子曰：“秦王有病召医，破痈溃痤者得车一乘 (12)，舐痔者得车五乘(13)，所治愈下得车愈多(14)。子岂治其痔邪(15)，何得车之多也？子行矣(16)！”
	　　[注释]
	　　(1)曹商：人名。
	　　(2)宋王：宋君偃。
	　　(3)说：通悦。
	　　(4)益：增加。
	　　(5)反：通返。
	　　(6)穷间：贫穷僻里。厄巷：狭巷。
	　　(7)困窘，贫苦。织屡，织鞋，做鞋。
	　　(8)槁项：干枯的脖子。馘（xù）：脸。
	　　(9)短：短处。
	　　(10)一：一旦。悟：使..觉悟。
	　　(11)长：长处。
	　　(12)痈：多个脓头的毒疮。痤（cuó）：痤疮，粉刺。一说疽。
	　　(13)舐（shì）：舔，痔，痔疮。
	　　(14)下：卑下。
	　　(15)岂：难道。
	　　(16)子：你。行：走。
	　　[译文]
	　　来国有个叫曹商的，为宋君偃出使秦国。刚去时，获得几辆车子。秦王喜欢他，增加车子百辆。返回宋国，见到庄子，说：“住在穷里狭巷，贫苦地靠织鞋而 生，搞得面黄肌瘦，这是我所短缺的；一旦使万乘之君主觉悟而使随从的车子增加到百乘，这是我的长处。”庄子说：“秦王有病召请医生，破除痈疽溃散痤疮的可 以得车一辆，舔痔疮的可以得车五辆，所医治的愈卑下得的车愈多。你难道治疗他的痔疮了吗？为什么你得到的车这么多呢？你走吧！”
	　　鲁哀公乎问颜阖曰(1)：“吾以仲尼为贞(2)，国其有疹乎(3)？”曰：“殆哉权乎仲尼(4)！方且饰羽而画(5)，从事华辞(6)，以支为旨 (7)，忍性以视民而不知不信(8)受乎心(9)宰乎神(10)夫何足以上民(11)！彼宜女与予颐与(12)， 误而可矣(13)！(，) 今使民离实(，) 学伪(14)，非(，) 所以视民也(15)。为后世虑，不若休之(16)。难治也(17)！”施于人而不忘(18)，非天布也(19)，商贾不齿(20)，虽以事齿之(21)， 神者弗齿(22)。为外刑者(23)，金与木也(24)；为内刑者(25)，动与过也(26)。宵人之离外刑者(27)，金木讯之(28)；离内刑者，阴 阳食之(29)。夫免乎外内之刑者，唯真人能之。
	　　[注释]
	　　(1)鲁哀公：春秋未年鲁国国君。见《德充符》、《让王》。颜阖：鲁国的贤人，见《人间世》、《达生》、《让王》诸篇。
	　　(2)贞干：古代筑墙的工具。立两端的为帧，坚两侧的为干。此处喻以孔于为辅相的意思。贞，同桢。
	　　(3)瘳乎：可治吗。瘳（chōu），病愈。
	　　(4)殆：危险。权：通岌，危。
	　　(5)羽：羽毛。饰羽而画：用画装饰有文彩的羽毛。

	　　无足问知和说：“众人没有不兴名求利的。他富有别人就依附他，依附他就于心居他之下，居下就对他尊敬。有人居下就显出尊贵的人，是长寿、体安、快乐之 道。现在，难道你没有这种想法吗？是智慧不足呢？还是认识到而力不从心呢？还是故意推行正道而不妄为呢？”知和说：“现在有这样一种人，自以为和自己同时 代生，同乡而处，就认为是个超过世俗的人。这是一种内心无主见取正，不能观察古今时间的差别和是非的区别。与世俗同化，离开自重，抛弃自尊，做自己兴名就 利的事情。这样论到延长生命、安适身体、愉悦心意之道，不是大远了吗？悲痛的疾病，安静的喜悦，不视其形体；惊慌的恐惧，欣欢的喜悦，不视其内心。只知道 去做而不知道为什么去做，所以地位贵到天子，富有占据天下，却不能免除祸患。”无足说：“富有对于人，无所不利。穷尽完美和究尽权势，至人不能得到，圣人 也不能达到。挟持别人勇敢和力量来增加威势，拿别人的智谋来增强观察问题的能力，用别人的品德当作贤良，不享有国土而威严如君主。况且声色、滋味、权势对 于人，心不等学习就爱好它，身体不等模仿就感到安适。欲求、厌恶、避害、就利，本来不用老师教导，这是人的本性。天下人虽然非难我，准能失掉这些呢！”知 和说：“有智慧的人做事，行动以百姓为基础，不违背民众为原则，因此知足而不相争，无所作为，所以不贪求。不知足，所以才贪求它，争夺声、色、味、权，而 自己却不认为是贪求。有剩余，所以有辞让，抛弃占据的天下之财而自己也不认为是清廉。清廉和贪求的实质，并不是受迫于外物，反过来要察看网心是否有度。权 势达到天子，而不拿权贵作为资本，骄做对待别人；财富占有天下，而不拿财富戏弄人。计算其祸患，考虑其反面，认为有害于本性，所以推辞而不接受，并不是要 以此追求名誉。尧、舜做帝王而和睦，并不是在天下推行仁政，而是不以追求美而损害性；善卷、许由可得帝位而不接受，并不是虚伪地对待辞让，而是不以政事损 害自己。这些都是趋利避害，而天下人称赞他们是贤人，就是说他们虽然有趋利避害的思想，他们并不是以兴名沽誉为目的。”无足说：“一定要保持其名声，身受 苦累拒绝美味，节约营养以维持生命，就是象久病危险而不死的人。”知和说：“平均是福，多余是害，凡物没有不这样的，而财富更是如此。现在的富人，耳朵要 听钟鼓管籥的声音，嘴巴要尝牛羊狗猪甜酒的滋味，以感受他的情意，遗忘他的事业，可以说是昏乱了。沉溺盛气之中，好象负重走向上坡，可以说是劳苦了；贪财 而带来敝病，贪权而带来竭尽精神，居静则沉溺，体肥则骄满，可以说有疾病了。为了求富趋利，所以积累财富高过墙也不知足，并且骄满而不舍，可以说是耻辱 了；积累财富而无用，衷心信服而不知舍弃，满心忧伤着急，追求增多而不知止，可以说是忧虑了；在家里就疑虑盗贼来窃取财物，在外面就畏俱寇盗的伤害，在家 周备楼窗严防，在外不敢单独行动，可以说是畏惧了；这六种，是天下的最大祸害，大家都遗忘掉而不知明察，等到祸患来到，想尽心思竭尽财产，只求象过去过一 天好日子，也达不到了。所以，想看那种名声看不到，想求得利禄得不着，缠绕意志牺牲身体去争夺名利，不也是迷惑吗！”


	　　[译文]
	　　孔子和柳下季结成朋友，柳下季的弟弟，名字叫盗跖。盗跖随从的士卒有九千人，横行天下，侵袭诸侯，穿室破户，赶走人家的牛马，掳夺人家的妇女，贪得无 厌忘却亲友，不顾忌父母兄弟，不祭把祖先；所到过的地方，大国严守城池，小国坚守城堡，亿万民众都感到痛苦。孔子对柳下季说：“做父亲的一定教导儿子，做 兄长的一定教育弟弟。如果做父亲的不能教导儿子，做兄长的不能教育弟弟，就没有人看重父子兄弟的亲属关系了。现在，先生是当世的才智之士，而弟弟却是盗 跖，成为天下的祸害，你不能教育他，我私下为你感到羞愧！我请你允许我为你前去进行说服他。”
	　　柳下季说：“先生说：‘做父亲的一定能教导儿子，做兄长的一定能教育弟弟。’如果儿子不听从父亲的教导，弟弟不受兄长的教育，虽然现在有先生的辩才， 又能把他怎样呢？况且，跖的为人，心境象涌泉一样不可抑制，意境象暴风一样不可测度，强悍足以使之对抗，雄辩足以掩饰过错，顺从他的心意就高兴，违逆他的 心意就发怒，轻易地用语言污辱别人，先生一定不要前去。”孔子不听柳下季的劝说，让颜回驾车，子贡陪乘，去会见盗跖。盗跖正在大山的阳面休整士卒，切碎人 肝而食之。孔子下车走上前，看见传命官，说：“鲁国人孔丘，听说将军高尚正义，敬请传令官传达。”传令官入内通报。
	　　盗跖听到此事，大怒，眼象明星，怒发冲冠，说：“这个人是不是鲁国的巧伪人孔丘？替我告诉他：‘你做花言造巧语，虚妄地称道文王、武王，头戴装饰象树 枝般的贵冠，腰缠死牛胁的皮带，余辞缨论，不耕而食，不织而衣，摇唇鼓舌，专生是非，用以迷惑天下的君主，使天下的书生，不务正业，装作孝弟，而侥幸得到 封侯富贵。你的罪恶严重，快滚回去吧！不然，我要用你的肝当作午餐。”孔子再一次通报说：“我幸运地得到柳下季的介绍，希望到帐幕下拜见。”传令宫又通 报。盗跖说：“让他到前面来！”孔子快步而进，避开席位退步快跑，再拜盗跖。盗跖大怒，叉开两脚，握剑瞪眼，声如母虎，说：“孔丘，你往前来！你要说的， 顺着我的意思就活，违逆我的心思就死！”孔子说：“我听说，凡天下的人有三种美德：生就而身躯魁梧，容貌美好无双，不分老幼贵贱，见到都喜欢他，这是上 德。智慧能包容天地，才能足以辨别各种事物，这是中德。勇猛、强悍、果敢，聚众率兵，这是下德。凡是人具有这一种德的，就足以南面称帝了。
	　　现在，将军兼有这三种德行，身高八尺二寸，面目有光泽，嘴唇犹如鲜红的丹砂，牙齿象排列的贝珠，声音宏亮，而名字叫盗腑。我孔丘私下替将军感到羞耻不 可取。将军有意听我的意见，我请求向南出使吴国和越国，向北出使齐国和鲁国，向东出使宋国和卫国，向西出使晋国和楚国。给将军建造周围数百里的大城，设立 数十万户的采邑，尊奉将军为诸侯，和天下的诸侯共同除旧布新。停战休兵，收养众弟兄，共祭祖先。这是圣人才士的行为，也是天下人的愿望。”盗跖大怒说： “孔丘，你往前来！凡是可以用利禄来规劝的，可以用语言来谏正的，都可以把他们叫做愚陋的平民。现在，身体高大，面目美好，人人见到都喜欢，这是我父母所 遗留的德性。孔丘你虽然不赞美我，难道我自己不知道吗？况且，我听说：‘好当面赞美人的人，也是好背后毁谤人的人。’
	　　现在孔丘你告诉我有大城民众，是想要用利禄规劝我，而言养我当顺民，怎么可以长久呢！城再大，也没有比天下更大的了。尧、舜虽然有天下，但子孙没有立 锥的地方；商汤和周武王立为天子，而后代灭绝；不正是他们贪大利的缘故吗？况且，我还听说：‘古代禽兽多而人少，于是人民都住在树巢中以躲避禽兽，白天捡 橡栗充饥，夜晚栖于树上，所以命名叫他们是有巢氏的民众。古代人不知穿衣服，夏天积蓄薪材，冬天就用来烧火取暖，所以命名叫他们是只知生存的民众。神农时 代，躺下时安静，起来时舒缓，人只知自己的母亲，不知道自己的父亲，和野兽共同相处，耕地吃饭，织布穿衣，没有相害的心，这是道德的极盛时代。然而黄帝不 能达到这种德，与量尤交战于啄鹿的郊野，流血百里。尧、舜做天子，设群臣，商汤流放他的君主，周武王杀殷纣。自此以后，以强大欺陵弱小，以势众侵暴寡少。 商汤、周武王以来，都是害人之徒。’现在你修习周文王、武王治国之道，掌握天下的舆论，用来教化后代，宽大的衣裳，浅薄的腰带，矫揉的言论，虚伪的行为， 用以迷惑天下的君主，而想要求取富贵，强盗之中再也没有比你更大的了。
	　　天下人为什么不把你叫做盗丘，而把我叫做盗跖呢？你以甜言蜜语说服子路，而使他跟随你。让子路摘掉他的高冠，解掉他的长剑，而受你教育，天下人都说： ‘孔丘能制止强暴禁止非礼行为。’其最终结果，子路想要杀死卫的国君，而事情没有成功，在卫国的东城门之上身躯破剁成肉酱，这是你教育的不成功。你不是自 称才士圣人吗？然而一再被赶出鲁国，在卫国被铲平足迹，在齐国穷途末路，在陈国和蔡国之间被围困，天下没有容身之处。你教育子路身躯被剁成肉酱，这样的祸 患，对上无法保护自己身体，对下无法做人。你的道哪里有什么可贵的呢？世上所推崇的，没有超过黄帝的，黄帝尚且不能全备德行，而战于涿鹿郊野，流血百里。 尧不慈爱，舜不孝顺，禹半身偏瘫，汤流放他的君主，武王讨伐纣王，文王被囚禁在麦里。这六个人，都是世上所推崇的。仔细地讨论这些事，都是用利禄迷失其本 真而强迫违反其性情，他们的行为乃是特别可耻的。世上所说的贤士：伯夷、叔齐。伯夷、叔齐辞去孤竹国的君主地位，而饿死在首阳山上，尸骨得不到埋葬：鲍焦 行为矫饰非议当世，抱着树木而死；申徒狄谏正国君而不听，背着石头投河自尽，为鱼鳖所食；介子推是最忠贞的，自己割下自己大腿上的肉，给晋文公吃，文公回 国后却背弃他，子推一怒之下而离开，抱着树而被烧死；尾生和女子相约在桥下，女子没来，河水来到，也不离开，抱着桥柱而死。这六个人，与分尸的狗、飘流的 死猪和持瓢乞丐，没有什么区别，都是重于名而轻于死，不惦念本真保养寿命的人。世上所说的忠臣，莫如王子比干、伍子肯。伍子胥被杀死尸沉入江中，比干被挖 心而死，这两个人，是世上所谓的忠臣，然而最终遭到天下人的耻笑。从上述来看，伍子胥，比干，都是不足推崇的。
	　　孔丘你用来说服我的，如果告诉我鬼的事情，则我不知道；如果告诉我人事，不过如此而已，都是我所听过的，现在我告诉你人的性情：眼睛要看颜色，耳朵要 听声音，嘴巴要品味道，志气要得到满足。人生上寿一百岁，中寿八十岁，下寿六十岁，除了疾病、死丧、忧患，其中张嘴而笑的，一月之中，不过四五天而已。天 地无穷尽，人死有时限，持有时限的生命，而寄托于无穷尽的天地之间，迅速得和骏马奔驰过缝隙一样。不能悦其意志，保养寿命的人，都不是通达道理的人。孔丘 你所说的话，都是我所抛弃的，赶快回去，不要再说了！你的道理，狂妄无度，心情急切，是诈巧虚伪的事情，不可以保全真性，还有什么可以值得讨论的呢！”孔 子再次叩拜，急忙跑出门外，上车，缰绳三次脱手，眼睛茫然不见，面色如同死灰，按着车轼低头，不能喘出气来。回到鲁国东门之外，正好遇见柳下季。柳下季 说：“近日怎么好几天没见到你，车马有出发的样子，莫非去见跖了吗？”孔子仰天而叹说：“是的。”柳下季说：“跖是不是象我从前所说的违背了你的意愿 呢？”孔子说：“是的。我此举是没病而自己针灸，急跑挑弄虎头，编织虎须，几乎不能免于虎口啊！”
	　　子张问于满苟得曰(1)：“盍不为行(2)？无行则不信(3)，无信则不任(4)， 不任则不利。故观之名(5)，计之利，而义真是也(6)。若弃名利，反之于心，则夫士之为行，不可一日不为乎！”满苟得曰：“无耻者富(7)，多信者显 (8)。夫名利之大者，几在无耻而信。故观之名，计之利，而信真是也。若弃名利，反之于心，则夫士之为行，抱其天乎(9)！”子张曰：“昔者桀、纣贵为天 子，富有天下。今谓臧聚曰(10)：‘汝行如桀、纣。’则有作色(11)，有不服之心者(12)，小人所贱也。仲尼、墨翟(13)，穷为匹夫，今谓宰相 曰：‘子行如仲尼、墨翟。’则变容易色(14)，称不足者，士诚贵也(15)。故势为天于，未必贵也；穷为匹夫，未必贱也。贵贱之分，在行之美恶。”满苟 得曰：“小盗者拘(16)，大盗者为诸侯。诸侯之门，义士存焉。昔者桓公小白杀兄入嫂(17)，而管仲为臣(18)；田成子常杀君窃国(19)，而孔子受 币(20)。论则贱之，行则下之，则是言行之情悻战于胸中也(21)，不亦拂乎(22)！故《书》曰：‘孰恶孰美，成者为首(23)不成者为尾。’”子张 曰：“子不为行，即将疏戚无伦(24)， 贵贱无义，长幼无序。五纪六位(25)，将何以为别乎？”满苟得曰：“尧杀长子，舜流母弟(26)，疏戚有伦乎？汤放桀，武王杀纣，贵贱有义乎？王季为適 (27)，周公杀兄(28)，长幼有序乎？儒者伪辞(29)，墨子兼爱(30)，五纪六位，将有别乎？”且子正为名(31)，我正为利。名利之实，不顺于 理，不监于道(32)。吾日与子讼于无约(33)，曰：‘小人殉财(34)，君子殉名，其所以变其情，易其性，则异矣(35)；乃至于弃其所为而殉其所不 为，则一也(36)。’故曰，无为小人，反殉而天；无为君子，从天之理。若在若直(37)，相而天极(38)。面观四方(39)，与时消息。若是若非，执 而圆机(40)；独成而意(41)，与道徘徊。无转而行(42)，无成而义(43)，将失而所为(44)。无赴而富(45)，无殉而成(46)， 将弃而天(47)，比干剖心，子胥抉眼(48)，忠之祸也；直躬证父(49)，尾生溺死，信之患也；鲍子立干(50)，申子不自埋(51)，廉之害也；孔 子不见母(52)，匡子不见父(53)，义之失也，此上世之所传，下世之所语，以为士者(54)，止其言(55)，必其行，故眼其殃，离其患也 (56)。”

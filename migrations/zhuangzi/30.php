
	　　[题解]
	　　《盗跖》以人名篇。“盗跖”指名叫跖的大盗。本篇的主旨在于揭露和批判儒家的道德规范和俗儒追求荣华富贵的观念，宣扬顺从自然本性的道家思想。有人认为本篇是膺品，非庄子作，此种看法不可轻信。
	　　在“孔子与柳下季为友”至“孔子再拜趋走”的几段中，庄子通过盗跖之日揭露孔子的言行，批判孔子的仁义说，讥斥儒家是“不耕而食，不织而衣”的人，说 孔子的言行是“诈巧虚伪”。在“子张问于满苟得”和“无足问于知和曰”的段落中，庄子又通过盗跖之口揭露了社会的黑暗，批判儒家伦理道德的实质是贵贱无 等，长幼无序，造成了天下之至害。并用先王之治的乱伦之例驳斥了宣扬圣土之治的一些观点，这对儒墨的尊先王的思想都是严厉地批判。从中可以看出儒墨显学之 争，也可以看出庄子作为道家与儒墨学派思想观点的不同。
	　　孔子与柳下季为友(1)，柳下季之弟，名曰盗跖(2)。盗跖从卒九千人(3)，横行天下，侵暴诸侯，穴室枢户(4)，驱人牛马，取人妇女，贪得忘亲， 不顾父母兄弟，不祭先祖。所过之邑，大国守城，小国入保(5)，万民苦之。孔子谓柳下季曰：“夫为人父者，必能诏其于(6)；为人兄者，必能教其弟。若父 不能诏其子，兄不能教其弟，则无贵父子兄弟之亲矣。今先生，世之才士也，弟为盗跖，为天下害，而不能教也，丘窃为先生羞之。丘请为先生往说之(7)。”
	　　柳下季曰：“先生言‘为人父者必能诏其子，为人兄者必能教其弟。’若子不听父之诏，弟不受兄之教，虽今先生之辩(8)，将奈之何哉！且跖之为人也，心 如涌泉(9)，意如飘风(10)，强足以拒敌(11)，辩足以饰非，顺其心则喜，逆其心则怒，易辱人以言，先生必无往。”孔子不听，颜回为驭(12)，子 贡为右(13)，往见盗跖。盗跖乃方休卒徒大山之阳(14)，脍人肝而餔之(15)。
	　　孔子下车而前，见谒者(16)，曰：“鲁人孔丘，闻将军高义，敬再拜谒者。”谒者人通。盗跖闻之，大怒，目如明垦，发上指冠，曰：“此夫鲁国之巧伪人 孔丘，非邪？为我告之：‘尔作言造语，妄称文武，冠枝木之冠(17)，带死牛之胁，多辞缪说，不耕而食，不织而衣，摇唇鼓舌，擅生是非，以迷天下之主，使 天下学士不反其本(18)，妄作孝弟，而侥幸于封，侯富贵者也。子之罪大极重，疾走归！不然，我将以子肝益昼浦之膳(19)！’”孔子复通曰：“丘得幸于 季，愿望履幕下。”谒者复通，盗跖曰：“使来前！”
	　　孔子趋而进(20)，避席反走(21)，再拜盗跃。盗跖大怒，两展其足，案剑瞑目(22)，声如乳虎，曰：“丘，来前！若所言，顺吾意则生，逆吾心则 死！”孔子曰：“丘闻之：‘凡天下有三德：生而长大，美好无双，少长贵贱见而皆说之(23)，此上德也；知维天地，能辩诸物，此中德也；勇悍果敢，聚众率 兵，此下德也。’凡人有此一德者，足以南面称孤矣。今将军兼此三者，身长八尺二寸，面目有光，唇如激丹(24)，齿如齐贝(25)，音中黄钟(26)，而 名曰盗历，丘窃为将军耻不取焉。将军有意听臣，臣请南使吴、越，北使齐、鲁，东使宋、卫，西使晋、楚，使为将军造大城数百里，立数十万户之邑，尊将军为诸 侯，与天下更始(27)，罢兵休卒，收养昆弟(28)，共祭先祖。此圣人才士之行，而天下之愿也。”
	　　盗跖大怒曰：“丘，来前！夫可规以利而可谏以言者，皆愚陋恒民之谓耳(29)。今长大美好，人见而悦之者，此吾父母之遗德也。丘虽不吾誉，吾独不自知 邪？且吾闻之：‘好面誉人者，亦好背而毁之。’今丘告我以大城众民，是欲规我以利而恒民畜我也(30)，安可久长也！城之大者，莫大乎天下矣。尧、舜有天 下，子孙无置锥之地；汤、武立为天子，而后世绝灭；非以其利大故邪？且吾闻之：‘古者禽兽多而人少，于是民皆巢居以避之，昼拾橡栗，暮栖木上，故命之曰有 巢氏之民。古者民不知衣服，夏多积薪，冬则炀之(31)，故命之曰知生之民。神农之世，卧则居居(32)，起则于于(33)，民知其母，不知其父，与糜鹿 共处，耕而食，织而衣，无有相害之心，此至德之隆也。然而黄帝不能致德，与蚩尤战于涿鹿之野(34)，流血百里。尧、舜作，立群臣，汤放其主，武王杀纣， 自是之后，以强陵弱，以众暴寡。汤、武以来，皆乱人之徒也。’今子修文、武之道，掌天下之辩，以教后世，缝衣浅带，矫言伪行，以迷惑天下之主，而欲求富贵 焉，盗奠大于子。天下何故不谓子为盗丘，而乃谓我为盗跖？子以甘辞说子路(35)，而使从之。使子路去其危冠，解其长剑，而受教于子，天下皆曰：‘孔丘能 止暴禁非。’其卒之也，子路欲杀卫君，而事不成，身范于卫东门之上(36)，是子教之不至也。子自谓才士圣人邪？则再逐于鲁，削迹于卫，穷于齐，围于陈、 蔡，不容身于天下。子教子路范，此患，上无以为身，下无以为人。子之道岂足贵邪？世之所高，莫若黄帝，黄帝尚不能全德，而战涿鹿之野，流血百里。尧不慈， 舜不孝，禹偏枯(37)，汤放其主，武王伐纣，文王拘羡里，此六子者，世之所高也。孰论之，皆以利惑其真而强反其情性，其行乃甚可羞也。世之所谓贤士，伯 夷、叔齐。
	　　伯夷、叔齐辞孤竹之君，而饿死于首阳之山，骨肉不葬；鲍焦饰行非世(38)，抱木而死；申徒狄谏而不听，负石自投于河，为鱼鳖所食；介子推至忠也 (39)，自割其股，以食文公，文公后背之，子推怒而去，抱木而燔死(40)； 尾生与女于期于梁下(41)，女子不来，水至，不去，抱梁柱而死，此六子者，无异于磔犬流豕，操瓢而乞者(42)，皆离名轻死，不念本养寿命者也。世之所 谓忠臣者，莫若王子比干、伍子胥。子胥沉江，比干剖心，此二子者，世谓忠臣也，然卒为天下笑。自上观之，至于子胥、比干，皆不足贵也。丘之所以说我者，若 告我以鬼事，则我不能知也；若告我以人事者，不过此矣，皆吾所闻知也。今吾告子以人之情，目欲视色，耳欲听声，口欲察味，志气欲盈。人上寿百岁，中寿八 十，下寿六十，除病瘦、死丧、忧患，其中开口而笑者，一月之中，不过四五日而已矣。天与地无穷，人死者有时，操有时之具而托于无穷之间，忽然无异骇骇之驰 过隙也。不能说其志意，养其寿命者，皆非通道者也。丘之所言，皆吾之所弃也，亟去走归，无复言之！子之道，狂狂汲汲(43)，诈巧虚伪事也，非可以全真 也，奚足论哉！”
	　　孔子再拜，趋走出门，上车，执辔三失(44)，目茫然无见，色若死灰，据拭低头，不能出气。归到鲁东门外，适遇柳下季。柳下季曰：“今者阀然数日不 见，车马有行色，得微往见跖邪(45)？”孔于仰天而叹曰：“然。”柳下季曰：“历得无逆女意若前乎？”孔子曰：“然。丘所谓无病而自灸也，疾走料虎头 (46)，编虎须，几不免虎口哉！”

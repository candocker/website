	　　子张问满苟得说：“为什么不修养品行？没有品行就不会取信于人，不能取信于人就不能被任用，不被任用就得不到利禄。所以观察名，计较利，而义才是真实 的。如果抛弃名利，反心自问，那么士大夫的作为行事，不可以一天不实行仁义！”满苟得说：“不知羞耻的人富有，多讲信誉的人显贵。名利大的人，几乎都是无 耻又善言信的人。所以观察名，计较利，而信才是真实的。如果抛弃名利，反心自问，那么士大夫的作为行事，只好保持其天性了。”子张说：“过去桀、纣尊贵到 做了天子，富有到占据天下。现在对奴仆和更夫说：‘你们的行为象桀、纣。’他们就会愤怒变色，就会产生不服的心理，因为小人也轻贱桀纣。孔丘、墨翟，穷困 得成为一般人，这时要对宰相说：‘你的行为象孔丘、墨翟。’他就会改容变色，自称赶不上，士大夫真是可贵。所以，权势为天子，未必可贵；穷困为一般人，未 必低贱。贵贱的区别在于品行的好坏。”满苟得说，“小偷被囚禁，大盗却成为诸侯，只要在诸侯那里，就有了仁义。从前齐桓公小白杀了哥哥纳嫂嫂为妻，而管仲 却做他的臣子；田常杀掉君主窃取国家政权，而孔子却接受他的钱币。言谈认为下贱的，而行动却去做这种下贱的事情。这样言论和行动在心中矛盾，岂不是很乱 吗！所以《书》说，‘谁好谁坏，成功的居上，不成功的居下。’”子张说：“你不修养品行，将会亲疏没有伦常，贵贱没有准则，长幼没有等次，五伦六位，将如 何区别呢？”满苟得说：“尧杀掉大儿子，舜流放亲弟弟，亲疏有伦常吗？汤放逐桀，武王杀纣有标准吗？王季代替嫡位，周公杀掉哥哥，长幼有序吗？儒者的虚伪 言辞，墨子的兼爱，五伦六位还有区别吗？况且你正在求名，我正在求利。其实名利，既不顺于理，又不明于道。我过去和你在无约面前争论，说：‘小人力财而 死，君子为名而死，他们之所以改变自己的真情，变更自己的本性则不相同；乃至于抛弃自己的所应当做的而殉难自己所不应当做的却是相同的。’所以说：‘不要 做小人，要反求于自然；不要做君子，要顺从自然的规律。是曲是直，看其自然规律。’面向四方，随时变化，是是是非，保持你的圆转枢机。独自顺遂你的意愿， 与道周旋。不要专执你的行为，不要成就你的仁义，这将会失掉你的本能，不要追求你的富贵，不要用殉难换取你的成功，这样将会舍弃你的天性。比干被剖心，伍 子肴遗嘱挖眼，这是忠的祸患；直躬证实父亲偷羊，尾生被水淹死，这是守信用的祸患；鲍焦站立枯死，申徒狄投河自杀，这是清廉的祸患；孔子见不到母亲，匡子 见不到父亲，这是义的过失。这些事情从上代传下来，下代还要传下来，以此为士大夫，端正言论，必定实行，所以才遭到它的灾殃，受到它的祸患。”
	　　无足问于知和曰(1)：“人卒未有不兴名就利者(2)。彼富则人归之(3)，归则下之(4)，下则贵之。夫见下贵者，所以长生安体乐意之道也。今子独 无意焉，知不足邪，意知而力不能行邪(5)，故推正不忘邪(6)？”知和曰：“今夫此人以为与已同时而生(7)，同乡而处者，以为夫绝俗过世之士焉 (8)；是专无主正(9)，所以览古今之时，是非之分也。与俗化世(10)，去至重，弃至尊，以为其所为也。此其所以论长生安体乐意之道，不亦远乎！惨怛 之疾(11)，恬愉之安(12)，不监于体(13)；怵惕之恐(14)，欣欢之喜，不监于心；知为为而不知所以为，是以贵为天子，富有天下，而不免于患 也。”无足曰：“夫富之于人，无所不利，穷美究势(15)，至人之所不得逮(16)，贤人之所不能及(17)， 侠人之勇力而以为威强(18)，秉人之知谋以为明察(19)，因人之德以为贤良(20)，非享国而严若君父(21)。且夫声色滋味权势之于人，心不待学而 乐之，体不待象而安之(22)。夫欲恶避就(23)，固不待师，此人之性也。天下虽非我(24)，孰能辞之(25)！”知和曰：“知者之为，故动以百姓， 不违其度(26)，是以足而不争，无以为故不求。不足故求之，争四处而不自以为贪(27)；有余故辞之，弃天下而不自以为廉(28)。廉贪之实，非以迫外 也，反监之度(29)。势为天子，而不以贵骄人；富有天下，而不以财戏人(30)。计其患，虑其反(31)，以为害于性(32)，故辞而不受也，非以要名 誉也。尧、舜为帝而雍(33)，非仁天下也，不以美害生也；善卷、许由得帝而不受(34)，非虚辞让也，不以事害已。此皆就其利，辞其害(35)而天下称 贤焉，则可以有之，彼非以兴名誉也。”无足曰：“必持其名(36)绝甘(37)，约养以持生(38)，则亦久病长厄而不死者也(39)。”知和曰：“平为 福，有余为害者，物莫不然，而财甚者也。今富人，耳营钟鼓管笛之声(40)，口嗛于刍豢醪醴之味(41)，以感其意(42)，遗忘其业..(43)，可谓 乱矣；该溺于冯气(44)，若负重行而上也，可谓苦矣；贪财而取慰(45)，贪权而取竭(46)，静居则溺(47)，体泽则冯(48)，可谓疾矣；为欲富 就利(49)，故满若堵耳而不知避，且冯而不舍，可谓辱矣；财积而无用，服膺而不舍(50)，满心戚醮(51)，求益而不止，可谓忧矣；内则疑劫请之贼， 外则畏寇盗之害，内周楼疏(52)，外不敢独行，可谓畏矣。此六者，天下之至害也，皆遗忘而不知察，及其患至，求尽性竭财，单以反一日之无故而不可得也 (53)。故观之名则不见(54)，求之利则不得(55)，综意绝体而争此(56)，不亦惑乎！”。
